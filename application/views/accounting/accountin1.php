                    <div class="row">
                        <div class="col-md-12">
                            <div class="post-case-single v1">
                                <div class="content-post v3">
                                    <div class="title-post mb0">
                                        <h4 class="head3">Understanding the GST Returns</h4>
                                    </div>
                                    <p class="pb20">
                                        For properly updating the invoices, Indian taxpayers and businesses have to file certain returns with the Government. These returns have to be mandatorily filed as any non-compliance towards the same may lead to disallowance of input tax credit, apart from attracting penalties and interests, etc. Proper filing of information and passing the same in the returns is a mandatory process for smooth flow of credit to the last recipient.
                                    </p>
                                    <p>
                                        The returns have been designed so that all transactions are in sync with each other and that no transaction is left unattended between the buyer and the seller. The tale starts from GSTR-1. All the data is stored in GSTN, which can be accessed by the users/taxpayers anytime online.
                                    </p>
                                </div>
                                <div class="content-post v1 pt20">
                                    <div class="title-post mb0">
                                        <h4 class="head4">Types of GST Returns to be Filed by Normal Taxpayers</h4>
                                    </div>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-1</h4>
                                    </div>
                                    <p class="pb20">
                                        The taxpayer records all his outward supplies of goods and services in details in this form. This has to be mandatorily done by the 10th of the next month. This will form the basis of all future flow and match for credit reconciliations. GSTR-1 is a detailed form containing 13 different heads. The critical headings are:
                                    </p>
                                    <ul class="listomy">
                                        <li>GSTIN of the Taxable Person – Auto populated result</li>
                                        <li>Name – Auto populated result</li>
                                        <li>Gross Turnover in Last Financial Year – This has to be filed only once. From next year onwards, this field will be auto populated</li>
                                        <li>The Period for which the return is being filed – Month & Year shall be available as a drop down for selection</li>
                                        <li>Taxable outward supplies – Here, IGST shall be filled only in the case of inter-state movement whereas CGST and SGST shall be filled in case of intra-state movement. Moreover, details of any exempted sales or sale at nil rate of tax shall also be mentioned here</li>
                                        <li>Outward Supplies to end customer, where the value exceeds Rs. 2.5 lakhs – Other than mentioned, all such supplies are optional in nature</li>
                                        <li>Any other supplies not covered in above 2 sections</li>
                                        <li>Debit Notes or Credit Notes Details</li>
                                        <li>Amendments to the details of any outward supplies of previous periods – This does not covers any changes by way of debit/credit notes</li>
                                        <li>Exempted, Nil-Rated and Non-GST Supplies – This is a Non-GST section. When the details of exempted sales or nil-rated sales have already been mentioned anywhere above, then only Non-GST shall be filled up here</li>
                                        <li>Export Sales</li>
                                        <li>Tax Liabil arising out of advance receipts</li>
                                        <li>Tax Paid</li>
                                    </ul>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-2A</h4>
                                    </div>
                                    <p class="pb20">
                                        It is available on the <b>11th of the next month</b> for the recipients to see and validate the information therein. Recipients have time between 11th – 15th of the next month to change any information, delete or add, based on their books of accounts.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-2</h4>
                                    </div>
                                    <p class="pb20">
                                        This form is the culmination of all inward supplies of goods and services as approved by the recipient of the services. The due date is 15th of the next month. It is auto-populated with the details of GSTR-2A. GSTR-2 shall include the following heads:
                                    </p>
                                    <ul class="listomy">
                                        <li>GSTIN of the Taxable Person – Auto populated result</li>
                                        <li>Name – Auto populated result</li>
                                        <li>The Period for which the return is being filed – Month & Year shall be available as a drop down for selection</li>
                                        <li>Details of all inward supplies – Auto populated with the details of GSTR-1. The taxable person can make any further addition or changes to the invoice here</li>
                                        <li>Changes to the inward supplies made for any previous period</li>
                                        <li>Import of Goods – Imports are treated as Inter-state supply and IGST shall be applicable on the same</li>
                                        <li>Import of Goods in earlier periods</li>
                                        <li>Services received from a person outside India (Import of Services)</li>
                                        <li>Import of Services in earlier periods</li>
                                        <li>Debit notes or Credit notes Details</li>
                                        <li>Amendments made to Debit or Credit notes of previous periods</li>
                                        <li>Inward supplies emanating from Unregistered persons</li>
                                        <li>Credits received from an Input Service Distributor – Auto populated from details of GSTR-6</li>
                                        <li>TDS credit from specified persons – Auto populated from details of GSTR-7</li>
                                        <li>TCS credit from E-Commerce operators – Auto populated from details of GSTR-8</li>
                                        <li>Input Tax Credit remaining to be taken against an invoice, from which initially a partial invoice was taken</li>
                                        <li>Reverse Charge tax liability</li>
                                        <li>Amendment to such reverse charge tax liability</li>
                                        <li>Tax Paid</li>
                                        <li>Input Tax Credit Reversals – A dropdown containing reasons for such reversals shall be made available</li>
                                        <li>Amendments to such Input Tax Credit Reversals</li>
                                        <li>Learn more about GSTR-2 Return Filing</li>
                                    </ul>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-1A</h4>
                                    </div>
                                    <p class="pb20">
                                        The form shall be auto-populated after filing of GSTR-2 on the <b>15th of the next month</b>, having all the correct or changed information. The supplier shall have the choice to accept or reject the changes made by the recipient. Following such acceptance, the GSTR-1 shall be revised to such extent.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-3</h4>
                                    </div>
                                    <p class="pb20">
                                        This form is auto prepared by 20th of the next month. It will have the details of all outward as well as inward supplies of goods and services as furnished in GSTR-1 and GSTR-2. After considering both the details, GSTN will determine your input tax credit availability or the amount of tax payable.<br />It will have the following details:
                                    </p>
                                    <ul class="listomy">
                                        <li>GSTIN of the Taxable Person – Auto populated result</li>
                                        <li>Name – Auto populated result</li>
                                        <li>Address of the person – Auto populated result</li>
                                        <li>The Period for which the return is being filed – Month & Year shall be available as a drop down for selection</li>
                                        <li>Total turnover
                                            <ol class="listomy2">
                                                <li>Export Turnover</li>
                                                <li>Taxable Turnover</li>
                                                <li>Non-GST Turnover</li>
                                                <li>Nil Rated or Exempted Turnover</li>
                                                <li>Total Turnover (Sum of 1-4)</li>
                                            </ol>
                                        </li>
                                        <li>Details of outward supplies
                                            <ol class="listomy2">
                                                <li>Inter-state supply to end customers</li>
                                                <li>Intra-state supply to end customers</li>
                                                <li>Inter-state supply to registered persons</li>
                                                <li>Intra-state supply to registered persons</li>
                                                <li>Exports</li>
                                                <li>Amendments to Sales Invoices, Debit Notes and Credit Notes</li>
                                                <li>Tax liability on such outward supplies</li>
                                            </ol>
                                        </li>
                                        <li>Details of inward supplies
                                            <ol class="listomy2">
                                                <li>Inter-State received</li>
                                                <li>Intra-State received</li>
                                                <li>Imports</li>
                                                <li>Amendments to Purchase invoices, Debit Notes and Credit Notes</li>
                                                <li>Tax liability on such inward supplies</li>
                                                <li>Reversals of Input Tax Credit</li>
                                            </ol>
                                        </li>
                                        <li>Total tax liability for the period</li>
                                        <li>TDS received for the period</li>
                                        <li>TCS received for the period</li>
                                        <li>ITC for the period</li>
                                    </ul>
                                    <p>Apart from the above details, a Part B has to be filed containing the details of,</p>
                                    <ul class="listomy">
                                        <li>Any taxes, interests, penalties or fees paid during the period</li>
                                        <li>Any refunds claimed during the period w.r.t. cash ledger</li>
                                    </ul>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-9</h4>
                                    </div>
                                    <p class="pb20">
                                        This is the annual return, which the taxpayer has to file by 31st December of the coming financial year. It is nothing but the accumulation of all 12 monthly GSTR-3 of the taxpayer. It would also include the amount of tax paid during the year, including details of exports or imports.
                                    </p>
                                    <p class="pb20">
                                        Apart from the above forms, the Government shall serve those taxpayers who fail to furnish the returns on time, notice in Form GSTR-3A. After the GSTR-3 is fully accepted for the month, then final input tax credit shall be communicated through form GST ITC-1. The details of ITC-1 has to be confirmed in due time to get the credit for that month. If the same is not done in due time, then it will disallow the credit for the month and will be computed as a tax liability for the month instead. 
                                    </p>
                                    <div class="title-post mb0">
                                        <h4 class="head4">Returns to be filed by Composition Tax Payers</h4>
                                    </div>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-4A</h4>
                                    </div>
                                    <p class="pb20">
                                        Similar to the GSTR-2A above, GSTR-4A is generated quarterly for composition scheme taxpayers. It has the details of the inward supplies as reported by suppliers in GSTR-1.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-4</h4>
                                    </div>
                                    <p class="pb20">
                                        With the auto-populated details of GSTR-4A, the taxpayer can furnish all his outward supplies here. The due date is 18th of the following month and has to be filed quarterly. It also contains the details of tax payable and payment of tax.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-9A</h4>
                                    </div>
                                    <p class="pb20">
                                        This is the annual return for all composition tax payers. It has to filed by 31st December of the coming financial year and includes all the quarterly returns filed by the composition tax payer.
                                    </p>
                                    <div class="title-post mb0">
                                        <h4 class="head4">Returns to be filed by Foreign Non-Resident Taxpayer</h4>
                                    </div>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-5</h4>
                                    </div>
                                    <p class="pb20">
                                        This is a detailed form containing the particulars of outward supplies, imports, tax paid, input tax availed and remaining stock. This has to be filed monthly within 20th of the next month or if the registration is given up, then within 7 days of such surrender or expiry of registration.
                                    </p>
                                    <div class="title-post mb0">
                                        <h4 class="head4">Returns to be filed by an Input Service Distributor</h4>
                                    </div>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-6A</h4>
                                    </div>
                                    <p class="pb20">
                                        This form will be generated by 11th of next month after the suppliers have filed their GSTR-1 on 10th of the next month. It will be auto-populated with the details of inward supplier made to them. It has to be filed on a monthly basis by the ISD.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-6</h4>
                                    </div>
                                    <p class="pb20">
                                        Once the details are confirmed or corrected by the ISD, then GSTR-6 will be generated. It has to be filed by the ISD by 13th of the next month. This is also a monthly filing.
                                    </p>
                                    <div class="title-post mb0">
                                        <h4 class="head4">Returns to be filed by a Tax Deductor</h4>
                                    </div>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-7</h4>
                                    </div>
                                    <p class="pb20">
                                        Details of the tax deductions made during the month have to be furnished here. The due date is 10th of the next month.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-7A</h4>
                                    </div>
                                    <p class="pb20">
                                        This is a TDS certificate, which is auto-generated upon filing the GSTR-7 by the tax Deductor. It will be available for the assessees to download and keep record of. It will contain details of the tax deducted and the total amount of payment made.
                                    </p>
                                    <div class="title-post mb0">
                                        <h4 class="head4">Return to be filed by an E-Commerce Portal</h4>
                                    </div>
                                    <div class="title-post">
                                        <h4 class="title">GSTR-8</h4>
                                    </div>
                                    <p class="pb20">
                                        This return shall contain all the supplies made by the E-Commerce seller and the amount of tax collected as well. It has to be filed by 10th of the next month.
                                    </p>
                                    <p class="pb20">For those assessees whose annual turnover exceeds INR 1 Crore, then a reconciliation statement in Form GSTR-9B has to be filed by 31st December of the next fiscal year. It has to be filed annually and is basically an audited annual accounts, duly certified by competent authority.Where the assessee is a Government body or a United Nations Body, then a monthly Form GSTR-11 has to be filed by 28thof the next month. These bodies have a UIN (Unique Identification Number) and hence will be required to furnish the details of inward supplies.Where a taxable person’s registration has been surrendered or cancelled, then a final return in Form GSTR-10 has to be filed within 3 months of such cancellation or registration. It will declare the input tax credit and capital goods held by the taxpayer, tax payable and paid at such time.The Government has automated all the forms together by bringing the same details on a real time basis in front of the taxpayers. The step, which is of paramount importance, is Step No. 1, i.e. FORM GSTR-1. It will form the basis of all further activities.</p>
                                    <p class="pb20">Any shortcomings or short filings of information in the details provided by the suppliers can be rectified, changed or deleted by the recipients in ample period of time. It is a seamless process that matches all information together to get the final credit figures and tax payable, if any.</p>
                                    <p class="pb20">The payment challans are also a very crucial part of all the filing process. Without them, it is not possible to clear tax payments and dues in due course of time and also claim credit.</p>
                                    <ul class="listomy">
                                        <li>PMT-1: An online tax liability register arising out of return or non-return related liabilities of the taxpayer.</li>
                                        <li>PMT-2: Credit balance online as in GSTN</li>
                                        <li>PMT-2A: Re-credit addition to the GSTN balance of a taxpayer</li>
                                        <li>PMT-3: Online cash ledger</li>
                                        Note: The above forms are maintained free of cost by the GSTN for each taxpayer. It can be accessed anytime through a User ID and Password, 24X7.
                                        <li>PMT-4: Challan for payment of GST</li>
                                        <li>PMT-5: Payment register for unregistered taxpayers</li>
                                        <li>PMT-6: Application for claiming missing credit</li>
                                    </ul>
                                    <p class="pb20">Where it is found that there is an excess credit available in the account of a taxpayer, then the taxpayer has an option to claim refund of such excess credit within prescribed time. The refund application forms are different for the State and Central Governments.</p>
                                    <p class="pb20">There are 10 forms prescribed for the Central Government, out of which only 5 are applicable for the State application. The main form is <b>RFD-01</b> where the application for refund is made.</p>
