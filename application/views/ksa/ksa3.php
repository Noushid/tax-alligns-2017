                                <h3 class="title">VAT IN SAUDI ARABIA (KSA)</h3>
                                <div class="title-post">
                                    <h4 class="headg">VAT IN KSA</h4>
                                </div>
                                <p class="pb20">
                                    Government of KSA has announced that GAZT (GENERAL AUTHORITY OF ZAKAT AND TAX) is going to implement VAT in the nation. By the implementation of the VAT rules and regulations declared by the GAZT, the major goods and services are expecting to be taxed at 5% from the first day of January 2018. To be complied with the Vat rules, the businesses in KSA have to be recorded their financial transactions to ensure that all financial records are accurate and updated. The VAT registration may be mandatory or voluntary for the businesses and which is limited by the turnover. The mandatory registration required businesses have to prepare books of accounts, maintain accounting records and present it before tax authorities as and when required.
                                </p>
                                <div class="title-post">
                                    <h4 class="headg">VAT Registration</h4>
                                </div>
                                <div class="title-post">
                                    <h4 class="head4">Mandatory Registration</h4>
                                </div>
                                <p class="pb20">
                                    All companies, businesses or entities which make an annual taxable supply of goods and services in excess of SAR 375,000 are legally required to register for VAT by 20th December 2017.
                                </p>
                                <p class="pb20">
                                    However, all taxable persons whose annual taxable supplies exceed the mandatory Registration threshold but do not exceed SAR 1,000,000 will be exempted from the requirement to register until 20th December 2018.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Voluntary Registration</h4>
                                </div>
                                <p class="pb20">Those which make an annual taxable supply of goods and services in excess of SAR 187,500 are eligible for voluntary Registration. Voluntary Registration provides significant benefits for the companies since it allows the deduction of input tax.</p>
                                <div class="title-post">
                                    <h4 class="head4">REGISTRATION OF NRIs VAT UNDER VAT</h4>
                                </div>
                                <p class="pb20">Non-Residents, who carry on economic activities but have no fixed place of business or fixed establishment in Saudi Arabia, will be required to register if they have the obligation to pay VAT in Saudi Arabia.</p>
                                <p class="pb20">All Non-Resident taxable persons must have one Tax Representative established in Saudi Arabia and who is approved by GAZT.GAZT may request documentation from the taxable person to evidence that the above requirements are met.</p>
                                <div class="title-post">
                                    <h4 class="headg">VAT registRATION OF businesses</h4>
                                </div>
                                <p class="pb20">In order to register for VAT, businesses must first be registered at GAZT for Zakat and Income Tax.</p>
                                <p class="pb20">Some large companies, particularly those already registered for other forms of tax in Saudi Arabia, will be auto-registered for VAT by GAZT:</p>
                                <div class="content-post v3">
