                                <div class="title-post">
                                    <h4 class="headg">VAT Return</h4>
                                </div>
                                <p class="pb20">A VAT eligible taxpayer pays VAT on his taxable purchases and collects VAT on his taxable supplies.</p>
                                <p class="pb20">In essence, the taxpayer is collecting VAT on behalf of the General Authority of Zakat and Tax (GAZT) and has the right to claim back VAT paid on his taxable purchases.</p>
                                <p class="pb20">Taxpayers will be required to file their returns to provide details of transactions related to taxable supplies and purchases. Following the submission of VAT return form, taxpayers will be asked to either pay the VAT liability or may claim back VAT refund depending on their situation. This set of activities is commonly referred to as <b>VAT return filing</b>.</p>
                                <p class="pb20">GAZT has the right to issue a new assessment which adjusts a prior assessment, providing formal notice to the taxable person.</p>
                                <div class="title-post">
                                    <h4 class="head4">Who and when files VAT returns?</h4>
                                </div>
                                <p class="pb20">Taxable persons which make an annual taxable supply of goods and services in excess of SAR 40,000,000 will be required to file VAT returns monthly.</p>
                                <p class="pb20">All other taxable persons will be required to file VAT returns quarterly. However, such persons may elect to file monthly returns subject to approval by GAZT. <br /> From the end of the tax period (as defined above) all taxable persons will have one month to file their VAT return. For example:</p>
                                <div class="content-post v3">
                                    <div class="list-post">
                                        <h4 class="title">If monthly returns are required, for the tax period 1 January 2018 to 31 January 2018 the VAT return must be filed by 28 February 2018</h4>
                                    </div>  
                                    <div class="list-post">
                                        <h4 class="title">If instead quarterly returns are required, for the tax period 1 January 2018 to 31 March 2018, the VAT return must be filed by 30 April 2018</h4>
                                    </div>
                                </div>
                                <div class="title-post">
                                    <h4 class="head4">How to file VAT returns ?</h4>
                                </div>
                                <p class="pb20">Taxable persons will need to file the VAT Return through the GAZT e-portal once available.</p>
                                <p >The VAT return form will require you to provide various details about your business during the tax period:</p>
                                <ol class="listomy">
                                    <li>Standard rated domestic sales</li>
                                    <li>Sales to registered customers in other GCC states</li>
                                    <li>Zero rated domestic sales</li>
                                    <li>Zero rated exports</li>
                                    <li>Exempt sales</li>
                                    <li>Standard rated domestic purchases</li>
                                    <li>Imports subject to VAT paid at customs</li>
                                    <li>Imports subject to VAT accounted for through the reverse charge mechanism</li>
                                    <li>Zero rated purchases</li>
                                    <li>Exempt purchases</li>
                                    <li>Corrections from previous period (up to 5,000 SAR)</li>
                                </ol>
                                <p class="pb20">You will also need to specify any relevant adjustments.</p>
                                <p class="pb20">Note that for corrections (due to an error or omission) of more than 5,000 SAR, the taxpayer will need to complete a voluntary disclosure form.</p>
                                <p class="pb20">Where the taxable person fails to file the VAT Return within the prescribed period, GAZT may make an assessment of the tax.</p>
