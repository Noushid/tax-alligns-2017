                                </div>
                                <p class="pb20">
                                    The government of Saudi Arabia is collecting income tax and Zakat from the businesses. But there are few conditions to be charged under income tax and Zakat. Only non-Saudi investors are liable for income tax in KSA. The citizens of the GCC countries are normally considered to be Saudi citizens for income tax purposes in Saudi Arabia.
                                </p>
                                <p>As per the Saudi income tax act, the following persons are liable to income tax:</p>
                                <ol class="listomy">
                                    <li>A resident capital company to the extent of its non-Saudi shareholding.</li>
                                    <li>A resident non-Saudi natural person who carries on activities in Saudi Arabia.</li>
                                    <li>A non-resident person who carries out activities in Saudi Arabia through a permanent establishment (PE).</li>
                                    <li>A non-resident person who has other income subject to tax from sources within Saudi Arabia.</li>
                                    <li>A person engaged in natural gas investment fields.</li>
                                    <li>A person engaged in oil and other hydrocarbon production.</li>
                                </ol>
                                <p class="pb20">The net adjusted profits will be taxable at a rate of 20%. Withholding tax (WHT) rates are between 5% and 20%. </p>
                                <div class="title-post">
                                    <h4 class="head4">OTHER RATES OF INCOME TAX</h4>
                                </div>
                                <p class="pb20">
                                    It should be noted that although the income tax rate is 20%, income from the following two activities is subject to different rates:
                                </p>
                                <div class="content-post v3">
                                    <div class="list-post">
                                        <h4 class="title">Natural Gas Investment Tax (NGIT) shall be determined on the basis of the internal rate of return (IRR) on the cumulative annual cash flows of the taxpayer derived from natural gas investment activities. The rate applicable will be 30% if the IRR is 8% or less. The rate increases progressively up to 85% if the IRR equals or exceeds 20%.</h4>
                                    </div>  
                                    <div class="list-post">
                                        <h4 class="title">Income from oil and hydrocarbon production is subject to tax at a rate ranging from 50% to 85% effective from 1 January 2017.</h4>
                                    </div>
