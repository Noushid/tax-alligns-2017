                                <div class="title-post">
                                    <h4 class="headg">ZAKAT IN KSA</h4>
                                </div>
                                <p class="pb20">
                                    In most cases, Saudi citizen investors are liable for Zakat, an Islamic assessment. Normally Zakat is payable is on the Zakath Base. Zakat base represents the net worth of the entity as calculated for Zakat purposes. Where a company is owned by both Saudi and non-Saudi interests, the portion of taxable income attributable to the non-Saudi interest is subject to income tax, and the Saudi share goes into the basis on which Zakat is assessed. Zakat is charged on the company’s Zakat base at 2.5%.
