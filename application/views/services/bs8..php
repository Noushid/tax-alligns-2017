                                <h3 class="title">Indian Subsidiary Company</h3>
                                <p class="pb20">
                                    An Indian subsidiary company means a company which is incorporated in India within the provisions of the Indian Companies Act, 2013 by one or more foreign corporation(s). Foreign company is a company or body corporate incorporated outside India which, has a place of business in India whether by itself or through an agent, physically or through electronic mode; and conduct any business activity in India in any other manner.
                                </p>
                                <p class="pb20">
                                    Simple definition give understanding that even a company incorporated outside India, has simple electronic present, which may be used for business in India is a foreign company
                                </p>
                                <div class="content-post v3">
                                    <div class="title-post">
                                        <h4 class="title">WHY Indian Subsidiary Company</h4>
                                    </div>
                                </div>
                                <p class="pb20">
                                    As mentioned above, an Indian subsidiary company means a company which is incorporated in India. The incorporation in India is subject to current ceiling/limit of foreign direct investment. Though a foreign corporation can start a business in India in various formats like, a project office, a representative office or by opening a branch office, in all such type the Indian entity so created remains a foreign entity which is subject to higher rates of taxes and stringent regulations. In case the foreign corporation has plans to operate in India on full-fledged scale just like an Indian company then a subsidiary company of such a foreign company is the answer. As an Indian Subsidiary Company, it shall be subject to the same set of rules which applies to any other Indian company.
