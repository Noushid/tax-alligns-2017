                                    <div class="title-post">
                                        <h4 class="head3">DSC and DIN:</h4>
                                    </div>
                                    <div class="title-post">
                                        <h4 class="headg">Digital Signatures Certificates (DSC):</h4>
                                    </div>
                                    <p class="pb20">
                                        A digital signature is an electronic signature, which is in the form of codes. It is used for signing the electronic forms, filed with ROC for incorporation of Company. It looks like a pen drive. Digital Signature cannot be used in physical documents. Digital signatures are protected with a PIN. It takes 1-2 days to get it issued, and it costs around Rs.800/- to Rs.1500/-. Every proposed Director must have a DSC for making an application to obtain the DIN.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">Director Identification Number (DIN):</h4>
                                    </div>
                                    <p class="pb20">
                                        DIN is a unique identification number required for a person to become a director of a company. DIN is issued by ROC office (Ministry of Corporate Affairs). It is similar to a PAN Card number. DIN is to be mentioned in documents while appointing a person as a director of a company. A shareholder or stockholder is an individual or institution (including a corporation) that legally owns a share of stock in a public or private corporation. It is mandatory to appoint seven shareholders at the time of incorporation. Directors appointed can act as a shareholder. For a shareholder DIN and DSC is not mandatory. 
                                    </p>
                                    <div class="title-post">
                                        <h4 class="head3">PROCEDURE OF REGISTRATION IN BRIEF</h4>
                                    </div>
                                    <p class="pb20">
                                        <b>1. Making of DINs and DSCs </b><br/>
                                        <b>2. Drafting of the perfect MOA and AOA of the Producer Company</b><br/>
                                        <b>3. Other documents deemed necessary.</b><br/>
                                        <b>4. Filing application for incorporation of the Company. (Form No. INC-29) </b>
                                    </p>
