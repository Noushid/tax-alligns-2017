                                    <div class="title-post">
                                        <h4 class="head4">MINIMUM REQUIREMENTS</h4>
                                    </div>
                                    <p class="pb20">
                                        Minimum 5 directors are mandatory to run a producer company. All directors must possess DIN and DSC. 
                                    </p>
                                    <p class="pb20">Minimum paid-up authorized capital to incorporate Producer Company is of Rs. 5 lakh. </p>
                                    <div class="title-post">
                                        <h4 class="head4">INCORPORATE THE PRODUCER COMPANY</h4>
                                    </div>
                                    <p class="pb20">
                                        Choose a name <br />Have a Company address <br />Appoint minimum of 10 members out of which appointment of five directors is mandatory. 
                                    </p>
                                    <div class="title-post">
                                        <h4 class="head4">Register Producer Company with MCA CHOOSE A NAME:</h4>
                                    </div>
                                    <p class="pb20">
                                        A producer company should be named using the following suffix “…..Producer Company Limited” appropriately indicating its status as a producer company. The word “private” is not used in the naming process and the absence of which does not indicate that the company is a “Public”. Ensure that name is not similar to any other Producer company. Also, do check if the name is not a registered trademark taken by anybody under the IP Act. Also, ensure that the name is not too generic to be accepted by the ROC. Avoid using abbreviations, adjectives. 
                                    </p>
                                    <div class="title-post">
                                        <h4 class="head4">Company Address: </h4>
                                    </div>
                                    <p class="pb20">
                                        You can open a company to your residential address also. There is no need to have a commercial place to open up a company. This office address can be changed at any time after incorporation of the company. Once you start-up is set up, stable and ready to move on to a nice corporate space you can change the registered office address by informing to the ROC office.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="head4">For Registered Office Address One needs:- </h4>
                                    </div>
                                    <p class="pb20">
                                        <b>If, Owned Premises:</b> Any latest utility bill i.e. Electricity bill/water bill/mobile bill in the name of the owner which should not be older than two months + NOC from the owner. 
                                    </p>
                                    <p class="pb20">
                                        <b>If, Rented Premises:</b> Any latest utility bill i.e. Electricity bill/water bill/mobile bill in the name of the owner which should not be older than two months + NOC from the owner + Rent agreement/Rent receipt. 
                                    </p>
                                    <div class="title-post">
                                        <h4 class="head4">Applicant:</h4>
                                    </div>
