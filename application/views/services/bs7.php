                                    <div class="title-post">
                                        <h4 class="title">WHY PRODUCER COMPANY</h4>
                                    </div>
                                    <p class="pb20">
                                        The Producer Company must deal primarily with the produce of its active Members and is allowed to carry on any of the following activities by itself or through other entities – on behalf of the members.
                                    </p>
                                    <div class="list-post">
                                        <h4 class="title">Processing including preserving, drying, distilling, brewing, vinting, canning and packaging of produce of its Members;</h4>
                                    </div>
                                    <div class="list-post">
                                        <h4 class="title">Manufacture, sale or supply of machinery, equipment or consumables mainly to its Members;</h4>
                                    </div>
                                    <div class="list-post">
                                        <h4 class="title">Providing education on the mutual assistance principles to its Members and others;</h4>
                                    </div>
                                    <div class="list-post">
                                        <h4 class="title">Rendering technical services, consultancy services, training, research and development and all other activities for the promotion of the interests of its Members;</h4>
                                    </div>
                                    <div class="list-post">
                                        <h4 class="title">Generation, transmission and distribution of power, revitalisation of land and water resources, their use, conservation and communication relatable to primary produce;</h4>
                                    </div>
                                    <div class="list-post">
                                        <h4 class="title">Insurance of producers or their primary produce;</h4>
                                    </div>
                                    <div class="list-post">
                                        <h4 class="title">Promoting techniques of mutuality and mutual assistance;</h4>
                                    </div>
                                    <div class="list-post">
                                        <h4 class="title">Welfare measures or facilities for the benefit of Members as may be decided by the Board;</h4>
                                    </div>
                                    <div class="list-post">
                                        <h4 class="title">Any other activity, ancillary or incidental to any of the activities which may promote the principles of mutuality and mutual assistance amongst the Members in any other manner;</h4>
                                    </div>
                                    <div class="list-post">
                                        <h4 class="title">Financing of procurement, processing, marketing or other activities which include extending of credit facilities or any other financial services to its Members.</h4>
                                    </div>
                                    <div class="list-post">
                                        <h4 class="title">To offer a statutory and regulatory framework that creates the potential for producer-owned enterprises to compete with other enterprises on a competitive footing.</h4>
                                    </div>
                                    <div class="list-post">
                                        <h4 class="title">To provide for the method of formation and registration of “Producer Companies” which, inter alia carries the principles of “mutual assistance” and “Co-operation” within the more liberal regulatory framework afforded by the company law with suitable adaptation.</h4>
                                    </div>
                                    <div class="list-post">
