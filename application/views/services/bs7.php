                                <h3 class="title">Producer Company</h3>
                                <p class="pb20">
                                    Traditionally the farmers in India were facing difficulties to market their produce and find better price was a risky task. All these were due to lack of organizational structure and complexity of co-operative societies. Now the Govt. of India made new form of business for the well being of farm producers that is termed as Producer Company. Producer Company is a form of business formation under which the farmers/producers can do business in a safe zone.
                                </p>
                                <p class="pb20">
                                    Producer Company is a company registered under the Companies Act 2013 and shall carry on any of the following activities: Production, harvesting, procurement, grading, pooling, handling, marketing, selling, export of primary produce of members or import of goods or services for their benefit; Processing including preserving, drying, distilling, brewing, venting, canning and packaging of produce of its members; and Manufacture, sale or supply of machinery, equipment or consumables mainly to its members. 
                                </p>
