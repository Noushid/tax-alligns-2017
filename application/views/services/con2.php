                                <div class="content-post v3">
                                    <p class="pb20">
                                        This guide helps you identify which customers to focus on and your key objectives in reaching them. It explains what to include in your marketing strategy and how it can be used as the basis for effective action.<br />Key elements of a successful marketing strategy<br />Understanding your strengths and weaknesses<br />Developing your marketing strategy<br />Tips and pitfalls
                                    </p>  
                                    <div class="title-post">
                                        <h4 class="headg">KEY ELEMENTS OF MARKETING STRATEGY</h4>
                                    </div> 
                                    <p class="pb20">
                                        One of the key elements of a successful marketing strategy is the acknowledgement that yours existing and potential customers will fall into particular groups or segments, characterized by their "needs". Identifying these groups and their needs through market research, and then addressing them more successfully than your competitors, should be the focus of your strategy.
                                    </p>
                                    <p class="pb20">
                                        You can then create a marketing strategy that makes the most of your strengths and matches them to the needs of the customers you want to target. For example, if a particular group of customers is looking for quality first and foremost, then any marketing activity aimed at them should draw attention to the high quality service you can provide.
                                    </p>
                                    <p class="pb20">
                                        Once this has been completed, decide on the best marketing activity that will ensure your target market know about the products or services you offer, and why they meet their needs.
                                    </p>
                                    <p class="pb20">
                                        This could be achieved through various forms of advertising, exhibitions, public relations initiatives, Internet activity and by creating an effective "point of sale" strategy if you rely on others to actually sell your products. Limit your activities to those methods you think will work best, avoiding spreading your budget too thinly.
                                    </p>
                                    <p class="pb20">
                                        A key element often overlooked is that of monitoring and evaluating how effective your strategy has been. This control element not only helps you see how the strategy is performing in practice, it can also help inform your future marketing strategy. A simple device is to ask each new customer how they heard about your business.
                                    </p>
                                    <p class="pb20">
                                        Once you have decided on your marketing strategy, draw up a marketing plan to set out how you plan to execute and evaluate the success of that strategy. The plan should be constantly reviewed so it can respond quickly to changes in customer needs and attitudes in your industry, and in the broader economic climate.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">STRENGTHS AND WEAKNESSES</h4>
                                    </div> 
                                    <p class="pb20">
                                        Your strategy must take account of how your business' strengths and weaknesses will affect your marketing.
                                    </p>
                                    <p class="pb20">
                                        Begin your marketing strategy document with an honest and rigorous SWOT analysis, looking at your strengths, weaknesses, opportunities and threats. It is a good idea to conduct some market research on your existing customers at this point, as it will help you to build a more honest picture of your reputation in the marketplace.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="head5">Weaknesses:</h4>
                                    </div>
                                    <p class="pb20">
                                        Personal and flexible customer service<br />Special features or benefits that your product offers<br />Specialist knowledge or skills
                                    </p>
                                    <div class="title-post">
                                        <h4 class="head5">Strengths:</h4>
                                    </div>
                                    <p class="pb20">
                                        Limited financial resources<br />Lack of an established reputation<br />Inefficient accounting systems
                                    </p>
                                    <div class="title-post">
                                        <h4 class="head5">Opportunities:</h4>
                                    </div>
                                    <p class="pb20">Increased demand from a particular market sector<br />Using the Internet to reach new markets<br />New technologies that allow you to improve product quality</p>
                                    <div class="title-post">
                                        <h4 class="head5">Threats:</h4>
                                    </div>
                                    <p class="pb20">The emergence of a new competitor<br />More sophisticated, attractive or cheaper versions of your product or service<br />New legislation increasing your costs<br />A downturn in the economy, reducing overall demand<br />Having done your analysis, you can then measure the potential effects each element may have on your marketing strategy.</p>
                                    <p class="pb20"></p>
                                    <div class="title-post">
                                        <h4 class="headg">DEVELOP MARKETING STRATEGY</h4>
                                    </div> 
                                    <p class="pb20">
                                        With an understanding of your business' internal strengths and weaknesses and the external opportunities and threats, you can develop a strategy that plays to your own strengths and matches them to the emerging opportunity. You can also identify your weaknesses and try to minimize them.
                                    </p>
                                    <p class="pb20">
                                        The next step is to draw up a detailed marketing plan that sets out the specific actions to put that strategy into practice.<br />Questions to ask when developing your strategy<br />What changes are taking place in our business environment? Are these opportunities or threats?<br />What are our strengths and weaknesses?<br />What do I want to achieve? Set clear, realistic objectives.<br />What are customers looking for? What are their needs?<br />Which customers are the most profitable?<br />How will I target the right potential customers? Are there groups that I can target effectively?<br />What's the best way of communicating with them?
                                    </p>
                                    <p class="pb20">
                                        Could I improve my customer service? This can be a low-cost way of gaining a competitive advantage over rivals, keeping customers, boosting sales and building a good reputation.
                                    </p>
                                    <p class="pb20">
                                        Could changing my products or services increase sales and profitability? Most products need to be continuously updated to maintain competitiveness.
                                    </p>
                                    <p class="pb20">
                                        Could extending my product list or service provision meet existing customers' needs more effectively? Remember that selling to existing customers is generally more cost effective than continually trying to find new ones.
                                    </p>
                                    <p class="pb20">
                                        How will I price my product or service? Although prices need to be competitive, most businesses find that trying to compete on price alone is a poor strategy. What else are my customers interested in? Quality? Reliability? Efficiency? Value for money?<br />What is the best way of distributing and selling my products?
                                    </p>
                                    <p class="pb20">
                                        How can I best promote my products? Options might include advertising, direct marketing, exhibiting at trade fairs, PR or marketing on the web.<br />How can I tell if my marketing is effective? Check how your customers find out about your business. A small-scale trial can be a good way of testing a marketing strategy without committing to excessive costs.
                                    </p>          
