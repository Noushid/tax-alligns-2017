                                <p class="pb20">
                                    A Trust can either be a private trust or a public charitable trust. Private trusts are governed by the Indian Trusts Act (1882) and are used for private purposes, such as running a private estate or institution. Private trusts are not given any tax benefits by the Government of India. If you want to do some charitable work for public –you can set up a public charitable trust. India does not have a national level law to govern charitable trusts. However, a few of the states have enacted Public Charitable Trusts Act (Like Bombay Public Trusts Act, 1950)
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">How to Form a Charitable Trust</h4>
                                </div>
                                <p class="pb20">
                                    A Trust is the obligation or responsibility placed on one in whom confidence or authority is place; it is a confidence reposed in a person by conveying to him the legal title to property which he is to hold for the benefit of others. Therefore, the “Trustee” responsibility includes protection of rightful ownership in the Trust property, the preservation of the Trust property and channelizing the income from the Trust property in accordance with the intentions of the creator of the Trust. In this article, we look at the procedure for forming a Charitable Trust in India.
