                                    <div class="title-post">
                                        <h4 class="head4">Definition of Charitable Trust in India</h4>
                                    </div>
                                    <p class="pb20">
                                        The The Indian Trusts Act, 1882 defines a Trust as an obligation annexed to the ownership of property, and arising out of a confidence reposed in and accepted by the owner, or declared and accepted by him, for the benefit of another, or of another and the owner.
                                    </p>
                                    <ol class="listomy">
                                        <li>The person who reposes or declares the confidence is called the “Author of the Trust”.</li>
                                        <li>The person who accepts the confidence is called the “Trustee”.</li>
                                        <li>The person for whose benefit the confidence is accepted is called the “Beneficiary”.</li>
                                        <li>The subject-matter of the trust is called “Trust Property” or “Trust Money”.</li>
                                        <li>The instrument if any, by which the Trust is declared is called the instrument of Trust or Trust Deed.</li>
                                    </ol>
                                    <div class="title-post">
                                        <h4 class="head4">Essential Elements for Trust Formation</h4>
                                    </div>
                                    <p class="">The following elements are essential for the formation of a Charitable Trust:</p>
                                    <p class="pb20 pl70"><b>
                                        1. An Author or Settler of the Trust<br />
                                        2. The Trustee<br />
                                        3. The Beneficiary<br />
                                        4. The Trust Property or the Subject Matter of the Trust<br />
                                        5. The objects of the Trust<br />

                                    </b></p>
                                    <p class="pb20">
                                        As per Section 6 of the The Indian Trusts Act, 1882 a Trust is created when the Author of the Trust indicates with reasonable certainty by any words or acts the following:
                                    </p>
                                    <ol class="listomy">
                                        <li>An intention on his part to created a Trust</li>
                                        <li>The purpose of the Trust</li>
                                        <li>The Beneficiary</li>
                                        <li>The Trust Property</li>
                                        <li>And transfers the Trust Property to the Trustee.</li>
                                    </ol>
                                    <div class="title-post">
                                        <h4 class="head3">Trust Deed</h4>
                                    </div>
                                    <p class="pb20">
                                        Trusts are registered using a document called TRUST DEED. This document contains all the information about the Trust and is printed/written/typed on plain A4 size papers. Along with these papers you would need to attach a Rs. 100 Non- Judicial stamp paper (which you can get from a notary). All the Trustees and witnesses will have to give thumb impressions and signatures on these papers. All in all, you will need help of a notary to prepare the papers. You may also need a No-Objection Certificate (NOC) from the owner of the property where the registered office of the trust is to be situated. If you’re the owner of the property, then you don’t need to worry about NOC
                                    </p>

                                    <div class="title-post">
                                        <h4 class="headg">Charitable Trust Deed:</h4>
                                    </div>
                                    <p class="pb20">
                                        It is the main instrument of any public charitable trust, wherein the aims and objects and mode of management (of the trust) should be enshrined. In every trust deed, the minimum and maximum number of trustees has to be specified. The trust deed should clearly spell out the aims and objects of the trust, how the trust should be managed, how other trustees may be appointed or removed, etc. The trust deed should be signed by both the Settlor/s and trustee/s in the presence of two witnesses.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">Registration of Charitable Trust Deed:</h4>
                                    </div>
                                    <p class="pb20">
                                        The Charitable Trust deed should be executed on non-judicial stamp paper, the value of which would depend on the valuation of the Charitable Trust property.
                                    </p>
                                    <p class="pb20">
                                        The application for registration should be made to the official having jurisdiction over the region in which the trust is sought to be registered.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">Elements Must be Mentioned in the Trust Deed:</h4>
                                    </div>
                                    <ol class="listomy">
                                        <li>1. Name and address of the Settler (Settler is the person who is setting up trust)</li>
                                        <li>2. Name and address of the other trustees</li>
                                        <li>3. Name of the trust</li>
                                        <li>4. Minimum and maximum number of trustees your trust can have</li>
                                        <li>5. Address of the registered office of the trust</li>
                                        <li>6. Objectives of the trust</li>
                                        <li>7. Rules and Regulations of the trust</li>

                                        <li>8. For registering a trust you need minimum two trustees (i.e. one settler and another person). You can decide the maximum number of trustees and this number must be mentioned in the trust deed. All the trustees together are called Board of Trustees. This board collectively governs the trust.</li>

                                        <li>9. Unlike societies, in case of trusts all or some of the trustees can be related persons (i.e. they may belong to the same family)</li>

                                        <li>10. All the trusts are allowed to work on all India level</li>

                                        <li>11. Trusts are irrevocable –unless it is mentioned in the trust deed. This means that the trust cannot be wound up</li>

                                        <li>12. Trustees are usually life-long members or their tenure is specified in the deed.</li>
                                        <li>13. Electoral process is not involved in the appointment of trustees.</li>

                                        <li>14. Board of Trustees can also have various designations for trustees. Common designations are Chairperson and Managing Trustee.</li>

                                        <li>15. Trustees cannot draw any remuneration from the trust fund. However, they may take reasonable compensation for the professional services they provide to the Trust.</li>

                                        <li>16. There no difference between a trust and a foundation.</li>

                                        <li>17. Profits earned by the Trust (e.g. interest gained from bank) cannot be distributed among the trustees.</li>

                                        <li>18. Trust Deed can be amended through a Supplementary Trust Deed.</li>

                                        <li>19. Most important part of the Trust Deed that you should pay special attention to is objectives of the trust. You should be as thorough as possible in writing down trust objectives so that you can function smoothly without any problems.</li>

                                        <li>20. At the time of registration, only the Settler and two witnesses are required to be present in front of the Sub-registrar under whose jurisdiction the registered office address comes. Sub-registrar will check IDs of these people. After that the Trust Deed will go to the counter where data entry takes place. In the end, Settler and two witnesses will be photographed. You will need to pay a fee of Rs. 1100 for this process. Of this amount, Rs. 100 will be the registration fee and Rs. 1000 will be the charges of keeping a copy of the Trust Deed with the sub-registrar. </li>

                                        <li>21. After about one week of submitting the papers, you can go back to the registrar’s office to receive a certified copy of the Trust Deed.</li>
                                    </ol> 
                                    <div class="title-post">
                                        <h4 class="head3">Trust Registration: Other Important Points</h4>
                                    </div>   
                                    <ol class="listomy">
                                        <li>1. Trust income is exempted from income tax. Bu to avail this facility, after registration, you need to acquire a certificate from the Income Tax Department. This certificate is called u/s 12A</li>

                                        <li>2. Donations to the public charitable trusts are also exempted from tax (i.e. the donor will not have to pay tax on the amount he donates to the trust). For this, you need to acquire 80G certificate from Income Tax Department.</li>

                                        <li>3. Usually lawyers and practicing CAs can help you in registration of trust –but I guess a lawyer is better person if you need help in formulating your Trust Deed. CAs doesn’t know the nitty-gritty of legal stuff.</li>

                                        <li>4. If you can write your own trust deed –and sure that you have done a good job –then all you need is the help of a notary who will take your Trust Deed and prepare it for presentation before the Registrar.</li>

                                        <li>5. After registration of the Trust, for acquiring 12A and 80G certificates, you may need to take services of a practicing CA. They charge hefty fee for these services.</li>
                                    </ol>                        
