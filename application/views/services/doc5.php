                                <div class="content-post v3">
                                    <div class="title-post">
                                        <h4 class="headg">2. Executive Summary</h4>
                                    </div>
                                    <p class="pb20">A project report must state the objectives of the business and the methods through which the business can attain success. The overall picture of the business with regard to capital, operations, methods of functioning and execution of the business must be stated in the project report. It must mention the assumptions and the risks generally involved in the business.</p>
                                    <div class="title-post">
                                        <h4 class="headg">3. Organization Summary</h4>
                                    </div>
                                    <p class="pb20">The project report should indicate the organization structure and pattern proposed for the unit. It must state whether the ownership is based on sole proprietorship, partnership or Joint Stock Company. It must provide information about the bio data of the promoters including financial soundness. The name, address, age qualification and experience of the proprietors or promoters of the proposed business must be stated in the project report.</p>
                                    <div class="title-post">
                                        <h4 class="headg">4. Project Description</h4>
                                    </div>
                                    <p class="pb20">A brief description of the project must be stated and must give details about the following:</p>
                                    <li class="listomy">
                                        <li>Location of the site,</li>
                                        <li>Raw material requirements,</li>
                                        <li>Target of production,</li>
                                        <li>Area required for the work shed,</li>
                                        <li>Power requirements,</li>
                                        <li>Fuel requirements,</li>
                                        <li>Water requirements,</li>
                                        <li>Employment requirements of skilled and unskilled labour,</li>
                                        <li>Technology selected for the project,</li>
                                        <li>Production process,</li>
                                        <li>Projected production volumes, unit prices,</li>
                                        <li>Pollution treatment plants required.</li>
                                    </li>
                                    <p class="pb20">If the business is service oriented, then it must state the type of services rendered to customers. It should state the method of providing service to customers in detail.</p>
                                    <div class="title-post">
                                        <h4 class="headg">5. Marketing Plan</h4>
                                    </div>
                                    <p class="pb20">
                                        The project report must clearly state the total expected demand for the product. It must state the price at which the product can be sold in the market. It must also mention the strategies to be employed to capture the market. If any, after sale service is provided that must also be stated in the project. It must describe the mode of distribution of the product from the production unit to the market. Project report must state the following
                                    </p>
                                    <ol class="listomy">
                                        <li>Type of customers,</li>
                                        <li>Target markets,</li>
                                        <li>Nature of market,</li>
                                        <li>Market segmentation,</li>
                                        <li>Future prospects of the market,</li>
                                        <li>Sales objectives,</li>
                                        <li>Marketing Cost of the project,</li>
                                        <li>Market share of proposed venture,</li>
                                        <li>Demand for the product in the local, national and the global market,</li>
                                        <li>It must indicate potential users of products and distribution channels to be used for distributing the product.</li>
                                    </ol>
                                    <div class="title-post">
                                        <h4 class="headg">6. Capital Structure and operating cost</h4>
                                    </div>
                                    <p class="pb20">
                                        The project report must describe the total capital requirements of the project. It must state the source of finance; it must also indicate the extent of owner’s funds and borrowed funds. Working capital requirements must be stated and the source of supply should also be indicated in the project. Estimate of total project cost, must be broken down into land, construction of buildings and civil works, plant and machinery, miscellaneous fixed assets, preliminary and preoperative expenses and working capital.
                                    </p>
                                    <p class="pb20">
                                        Proposed financial structure of venture must indicate the expected sources and terms of equity and debt financing. This section must also spell out the operating cost
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">7. Management Plan</h4>
                                    </div>
                                    <p class="pb20">The project report should state the following.</p>
                                    <ol class="listomy">
                                        <li>Business experience of the promoters of the business,</li>
                                        <li>Details about the management team,</li>
                                        <li>Duties and responsibilities of team members,</li>
                                        <li>Current personnel needs of the organization,</li>
                                        <li>Methods of managing the business,</li>
                                        <li>Plans for hiring and training personnel,</li>
                                        <li>Programs and policies of the management</li>
                                    </ol>
                                    <div class="title-post">
                                        <h4 class="headg">8. Financial Aspects</h4>
                                    </div>
                                    <p class="pb20">
                                        In order to judge the profitability of the business a projected profit and loss account and balance sheet must be presented in the project report. It must show the estimated sales revenue, cost of production, gross profit and net profit likely to be earned by the proposed unit. In addition to the above, a projected balance sheet, cash flow statement and funds flow statement must be prepared every year and at least for a period of 3 to 5 years.
                                    </p>
                                    <p class="pb20">
                                        The income statement and cash flow projections should include a three-year summary, detail by month for the first year, and detail by quarter for the second and third years. Breakeven point and rate of return on investment must be stated in the project report. The accounting system and the inventory control system will be used is generally addressed in this section of the project report. The project report must state whether the business is financially and economically viable.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">9. Technical Aspects</h4>
                                    </div>
                                    <p class="pb20">
                                        Project report provides information about the technology and technical aspects of a project. It covers information on Technology selected for the project, Production process, capacity of machinery, pollution control plants etc.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">10. Project Implementation</h4>
                                    </div>
                                    <p class="pb20">
                                        Every proposed business unit must draw a time table for the project. It must indicate the time within the activities involved in establishing the enterprise can be completed. Implementation schemes show the timetable envisaged for project preparation and completion.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">11. Social responsibility</h4>
                                    </div>
                                    <p class="pb20">
                                        The proposed units draw inputs from the society. Hence its contribution to the society in the form of employment, income, exports and infrastructure. The output of the business must be indicated in the project report.
                                    </p>                
