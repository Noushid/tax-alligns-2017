                                <h3 class="title">LLP in India</h3>
                                <p class="pb20">
                                    With the introduction of the Limited Liability Partnership (LLP) in India through the Limited Liability Partnership Act, 2008, the prominence of Partnership’s has been replaced by the LLPs. LLPs are easy to register, offer a range of benefits to the promoters and is easy to maintain, making it ideal for many small and medium sized business that would otherwise opt to start as a Proprietorship or a Private Limited Company. The Minimum Capital contribution are 1,00,000 for a Private Company – 5,00,000 for a Public Company. No such mandatory requirement and moreover, the contribution of a partner may consist of tangible, movable or immovable or intangible property or other benefit to the LLP.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Limited Liability Partnership as an Entity</h4>
                                </div>
                                <p class="pb20">
                                    LLPs are registered under the Limited Liability Partnership Act, 2008. The Partners of a LLP are not personally liable for the liabilities of the Partnership and the liability of a Partner is limited to the amount of his/her capital contribution to the LLP. Therefore, the LLP and the Partners of a LLP are considered to be separate legal entities and the LLP has a perpetual existence, until dissolved by the Promoters.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Number of Partners & Requirement</h4>
                                </div>
                                <p class="pb20">
                                    Any Indian Citizen residing in India can be a Partner in a LLP. Foreign Direct Investment is allowed in a LLP with prior RBI approval. Minors are however not allowed to be part of a LLP. A LLP must have a minimum of 2 Partners and is allowed to have unlimited Partners. The LLP Agreement governs aspects of management of a LLP and one or more Partners can be designated to manage the activities of the LLP.
                                </p>
