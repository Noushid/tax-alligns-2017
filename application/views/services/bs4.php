                                <div class="title-post">
                                    <h4 class="head4">Transferability or Conversion</h4>
                                </div>
                                <p class="pb20">
                                    The share of a LLP can be transferred. However, the Transferee is not allowed to become a Partner automatically. The share of a LLP can be transferred to another person more easily. A LLP can be converted into a Private Limited Company or a Limited Company easily.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Compliance & Taxation</h4>
                                </div>
                                <p class="pb20">
                                    The Profits of a LLP are taxed at 30% + educational cess. LLP must file annual return with the Ministry of Corporate Affairs (MCA).
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Registration</h4>
                                </div>
                                <p class="pb20">
                                    LLPs are registered with the Ministry of Corporate Affairs. LLP registration process is similar to that of a Private Limited Company Incorporation process, viz. obtaining Digital Signature Certificate for the Partners, obtaining Designated Partner Identification Number (DPIN) for the Partners, obtaining name approval from MCA, obtaining Incorporation Certificate and filing LLP Agreement.
                                </p>
                                <div class="title-post">
                                    <h4 class="head3">REASONS FOR CHOOSING LLP</h4>
                                </div>
                                <p class="pb20">
                                    There are a number of reasons why many entrepreneurs prefer to go in for a Limited Liability Partnership over a Private Limited Company. It is considered easier to set up, as a rule is comparatively hassle-free in day to day operations, has significantly lower burdensome compliance requirements and costs, and therefore many see it as advantageous to begin their organization in this manner. Let us look at some of the reasons for this choice and the LLP Advantages.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">No requirement of minimum contribution</h4>
                                </div>
                                <p class="pb20">
                                    As against company there is no minimum capital requirement in LLP. An LLP can be formed with least possible capital. The particulars of Minimum Capital contribution are 1. Private Company – 1,00,000; 2. Public Company – 5, 00,000; no such mandatory requirement and moreover, the contribution of a partner may consist of tangible, movable or immovable or intangible property or other benefit to the LLP.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">No limit on owners of business</h4>
                                </div>
                                <p class="pb20">
                                    An LLP requires a minimum 2 partners while there is no limit on the maximum number of partners; this is in contrast to a private limited company wherein there is a restriction of not having more than 200 members.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Lower Registration Cost</h4>
                                </div>
                                <p class="pb20">
                                    The cost of registering LLP is low as compared to cost of incorporating a private limited or a public limited company. An illustration can show the approximate cost involved in formation of private limited company and an LLP.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">No requirement of compulsory Audit</h4>
                                </div>
                                <p class="pb20">
                                    All limited companies, whether private or public, irrespective of their share capital, are required to get their accounts audited. But in case of LLP, there is no such mandatory requirement. This is perceived to be a significant compliance benefit. A Limited Liability Partnership is required to get the audit done only in the case that:-<br />
                                    1. The contributions of the LLP exceeds Rs. 25 Lakhs, or<br />
                                    2. The annual turnover of the LLP exceeds Rs. 40 Lakhs<br />
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Savings from lower compliance burden</h4>
                                </div>
                                <p class="pb20">
                                    Every year, there are about 8 to 10 regulatory formalities and compliances are required to be duly completed and submitted by a Private limited company whereas a Limited Liability Partnership is required to file only two, namely, the Annual Return & Statement of Accounts and Solvency.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Taxation Aspect on LLP</h4>
                                </div>
                                <p class="pb20">
                                    For income tax purpose, LLP is treated on a par with partnership firms. Thus, LLP is liable for payment of income tax and share of its partners in LLP is not liable to tax. Thus no dividend distribution tax is payable. Provision of ‘deemed dividend’ under income tax law, is not applicable to LLP. Section 40(b): Interest to partners, any payment of salary, bonus, commission or remuneration allowed as deduction.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Dividend Distribution Tax (DDT) not applicable</h4>
                                </div>
                                <p class="pb20">
                                    In the case of a company, if the owners to withdraw profits from company, an additional tax liability in the form of DDT @ 15% (plus surcharge & education cess) is payable by company. However, no such tax is payable in the case of LLP and profits of a LLP can be easily withdrawn by the partners.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Renowned form of business: </h4>
                                </div>
                                <p class="pb20">
                                    Though the concept of Limited Liability Partnership has been recently introduced in India but it is very known concept in other countries of the world especially in service sector.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Easy to Form: </h4>
                                </div>
                                <p class="pb20">
                                    It is very easy to form LLP, as the process is very simple as compared to Companies and does not involve much formality. Moreover, in terms of cost the minimum fees of incorporation is as low as Rs 800 and maximum is Rs 5600.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Body Corporate:</h4>
                                </div>
                                <p class="pb20">
                                    Just like a Company, LLP is also body corporate, which means it has its own existence as compared to partnership. LLP and its Partners are distinct entity in the eyes of law. LLP will know by its own name and not the name of its partners.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Liability: </h4>
                                </div>
                                <p class="pb20">
                                    A LLP exists as a separate legal entity from your personal life. Both LLP and person, who own it, are separate entities and both functions separately. Liability for repayment of debts and lawsuits incurred by the LLP lies on it and not the owner. Any business with potential for lawsuits should consider incorporation; it will offer an added layer of protection.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Perpetual Succession: </h4>
                                </div>
                                <p class="pb20">
                                    An incorporated LLP has perpetual succession. Notwithstanding any changes in the partners of the LLP, the LLP will be a same entity with the same privileges, immunities, estates and possessions. The LLP shall continue to exist till its wound up in accordance with the provisions of the relevant law.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Flexible to Manage: </h4>
                                </div>
                                <p class="pb20">
                                    LLP Act 2008 gives LLP the at most freedom to manage its own affairs. Partner can decide the way they want to run and manage the LLP, in form of LLP Agreement. The LLP Act does not regulated the LLP to large extent rather than allows partners the liberty to manage it as per their will and fancies.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Easy Transferable Ownership:</h4>
                                </div>
                                <p class="pb20">
                                    It is easy to become a Partner or leave the LLP or otherwise it is easier to transfer the ownership in accordance with the terms of the LLP Agreement.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Separate Property: </h4>
                                </div>
                                <p class="pb20">
                                    A LLP as legal entity is capable of owning its funds and other properties. The LLP is the real person in which all the property is vested and by which it is controlled, managed and disposed off. The property of LLP is not the property of its partners. Therefore partners cannot make any claim on the property in case of any dispute among themselves.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Taxation:</h4>
                                </div>
                                <p class="pb20">
                                    Another main benefit of incorporation is the taxation of a LLP. LLP are taxed at a lower rate as compared to Company. Moreover, LLP are also not subject to Dividend Distribution Tax as compared to company, so there will not be any tax while you distribute profit to your partners.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Raising Money: </h4>
                                </div>
                                <p class="pb20">
                                    Financing a small business like sole proprietorship or partnership can be difficult at times. A LLP being a regulated entity like company can attract finance from PE Investors, financial institutions etc.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Capacity to sue: </h4>
                                </div>
                                <p class="pb20">
                                    As a juristic legal person, a LLP can sue in its name and be sued by others. The partners are not liable to be sued for dues against the LLP. 
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">No Mandatory Audit Requirement: </h4>
                                </div>
                                <p class="pb20">
                                    Under LLP, only in case of business, where the annual turnover/contribution exceeds Rs 40 Lacs/Rs 25 Lacs are required to get their account audited annually by a chartered accountant. This provides great relief to small businessmen.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Partners are not agent of other Partners: </h4>
                                </div>
                                <p class="pb20">
                                    In LLP, Partners unlike partnership are not agents of the partners and therefore they are not liable for the individual act of other partners in LLP, which protects the interest of individual partners. 
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">Compliances: </h4>
                                </div>
                                <p class="pb20">
                                    As compared to a private company, the number of compliances is on lesser side in case of LLP.
                                </p>
                                <div class="title-post">
                                    <h4 class="head4">CONSUFED TO START A BUSINESS: LLP OR PARTNERSHIP</h4>
                                </div>
                                <p class="pb20">
                                    As the year draws to a close, everyone is asking the same question – Should I form a Partnership or a Limited Liability Partnership? Which option will suit me better?
                                </p>
                                <p class="pb20">
                                    Technically, a Limited Liability Partnership has many advantages. In fact, it would not be wrong to say that an LLP combines some of the advantages of a Private Limited Company with some of the advantages of a simple Partnership.
                                </p>
                                <p class="pb20">
                                    On the flip side, a simple Partnership is much cheaper to form, unless you decide to register it. On the special request of a client, when we decided to register a Partnership deed in post- Anna Hazare India, we found that touts and agents were charging us between Rs. 7000 and Rs. 15,000 just to register the simple Partnership. It would actually have been much cheaper to form a LLP, where the registration is completely online.
                                </p>
                                <p class="pb20">
                                    So coming back to the main question we need to answer here, let us compare some of the features of a Partnership vis-à-vis an LLP.
                                </p>
                                <p class="pb20">
                                    From the comparison above, it seems very clear that a Partnership has certain limitations. We also recommend that in most cases, if you want to choose between the two, it makes a lot of sense to choose the limited liability partnership route. Even from the point of view of drafting contracts and interacting with third parties like investors, having an LLP is safer (although investors love Private Limited Companies).
                                </p>
                                <p class="pb20">
                                    If you want to form a Partnership for any reason, please note that it need not be registered. You can do business perfectly legally without registering the Partnership. The only disadvantage is that if there is a Court case, you need to fight in the name of the individual partners (which is not a major disadvantage since most people never fight a court case). So if you want to form a Partnership, go ahead and form one but steer clear of the burden of having it registered.
                                </p>
                                <p class="pb20">
                                    Finally, one major concern of many people is regulations and accounts. People choose to form a simple partnership because they do not want the legal regulations and secretarial norms that come along with forming a LLP or a Pvt. Ltd. Company. Worry not, the complexity is slightly overrated. While a Partnership is definitely easier from a regulatory point of view, all the basic procedures – getting a GST / IT registration, getting a PAN & TAN Card, opening a fresh bank account and getting an IEC still have to be complied with, even if you start as a simple Partnership. And if your income is above the taxable limit, you need to file tax returns irrespective of whether you are a simple Partnership, LLP or a Private Limited Company.
                                </p>
