                                        <h4 class="headg">Hierarchical Structure</h4>
                                    </div> 
                                    <p class="pb20">
                                        The hierarchy has permeated virtually every company around the world regardless of size, industry, or location. The greatest strength of the hierarchy used to be that it was so reliable at maintain the status quo, which was exactly what companies wanted decades ago. However what was once its strength is now its greatest weakness. The hierarchy is a very resilient management structure that has been so embedded in how we work that most organizations around the world are having a tedious time getting rid of it. A hierarchical organization follows the layout of a pyramid. Every employee in the organization, except one, usually the CEO, is subordinate to someone else within the organization. The layout consists of multiple entities that descend into the base of staff level employees, who sit at the bottom of the pyramid.
