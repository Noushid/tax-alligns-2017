                                        <div class="title-post">
                                            <h4 class="title">MERITS OF FLAT STRUCTURE</h4>
                                        </div>
                                        <div class="list-post">
                                            <h4 class="title">It elevates the employees’ level of responsibility in the organization.</h4>
                                        </div>
                                        <div class="list-post">
                                            <h4 class="title">It removes excess layers of managements improves the coordination and speed of communication between employees.</h4>
                                        </div>
                                        <div class="list-post">
