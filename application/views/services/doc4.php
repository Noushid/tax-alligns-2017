                                    <h4 class="head4">What is Wakf ?</h4>
                                </div>
                                <p class="pb20">
                                    Wakf is an Islamic endowment of property to be held in trust and used for a charitable or religious purpose or a Muslim religious or charitable foundation created by an endowed trust fund. Wakf means the permanent dedication by a person professing Islam of any movable or immovable property, for any purpose recognized by the Muslim Law auspicious, religious or charitable.
                                </p>
