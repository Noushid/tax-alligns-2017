            <div class="container">
                <div class="case-single v1">
                    <div class="row">
                        <div class="col-md-12">
                            <div class="post-case-single v1">
                                <div class="content-post v3">
                                    <ol class="listomy">
                                        <li>An Application for registration shall be made in such form and manner and at such place as the Board may by regulation provide and shall contain following particulars.-
                                            <ol class="pl70 listomy">
                                                <li>a description of the wakf properties sufficient for identification thereof;</li>
                                                <li>the gross annul income from such properties;</li>
                                                <li>the amount of land revenue, cesses, rates and taxes annually payable in respect of the wakf properties;</li>
                                                <li>an estimate of the expenses annually incurred in the realisation to the income of the wakf properties;</li>
                                                <li>the amount set apart under the wakf for-
                                                    <ol class="listomy pl140">
                                                        <li>the salary of the mutawalli and allowances to the individuals;</li>
                                                        <li>purely religious purposes;</li>
                                                        <li>charitable purposes;and</li>
                                                        <li>any other purposes;</li>
                                                        <li>any other particulars provided by the Board by regulations.</li>
                                                    </ol>
                                                </li>
                                            </ol>
                                        </li>
                                    </ol>
                                    <ol class="listomy">
                                        <li>Every such application shall be accompanied by a copy of the wakf deed or if no such deed has been executed or a copy thereof cannot be obtained, shall contain full particulars, as far as they are known to the applicant, of the origin, nature and objects of the wakf.</li>
                                        <li>Every application made under sub section (2) shall be signed and verified by the applicant in the manner provided in the code of civil procedure,1908 (5 of 1908) for the signing and verification of pleadings.</li>
                                        <li>The Board may require the applicant to supply any further particulars or information the it may consider necessary.</li>
                                        <li>On receipt of an application for registration, the Board may, before the registration of the wakf make such inquiries as it thinks fit in respect of the genuineness and validity of the application and correctness of any particulars therein and when the application is made by any person other than the person administering the wakf property, the Board shall, before registering the wakf, give notice of the application to the person administering the wakf property and shall hear him if he desires to be heard.</li>
                                        <li>In the case of wakfs created before the commencement of this Act, every application for registration shall be made, within three months from such commencement and in the case of wakfs created after such commencement, within three months from the date of the creation of the wakf:</li>
                                    </ol>
                                    <p class="pb20">Provided that where there is not Board at the time of creation of a wakf, such application will be made within three months from the date of establishment of the Board. </p>
                                    <div class="title-post">
                                        <h4 class="headg">Register of wakfs.- </h4>
                                    </div>
                                    <p class="pb20">
                                        The Board shall maintain a register of wakfs which shall contain in respect of each wakf copies of the wakf deeds, when available and the following particulars, namely.-
                                    </p>
                                    <ol class="listomy">
                                        <li>the class of the wakf;</li>
                                        <li>the name of the mutawalli;</li>
                                        <li>the rule of succession to the office of mutawalli under the wakf deed or by custom or by usage; </li>
                                        <li>particulars of all wakf properties and all title deeds and documents relating thereto;</li>
                                        <li>particulars of the scheme of administration and the scheme of expenditure at the time of registration;</li>
                                        <li>such other particulars as may be provided by regulations.</li>
                                    </ol>
                                    <div class="title-post">
                                        <h4 class="headg">Powers of Board to appoint Executive Officer.-</h4>
                                    </div>
                                    <p class="pb20">
                                        <b>(1) </b>Notwithstanding anything contained in this Act, the Board may, if it is of the opinion that it is necessary so to do in the interests of the wakf, appoint on whole-time basis or part-time basis or in an honorary capacity, subject to such conditions as may be provided by regulations, an Executive Officer with such supporting staff as it considers necessary for any wakf having a gross annual income of not less than five lakhs rupees:<br/><br />Provided that the person chosen for appointment should be a person professing Islam.
                                    </p>
                                    <p class="pb20">
                                        <b>(2) </b>Every Executive Officer appointed under sub-section (1) shall exercise such powers and discharged such duties as pertain only to the administration of the property of the wakf for which he has been appointed and shall exercise those powers and discharge those duties under the direction, control and supervision of the Board:<br/><br />Provided that the Executive Officer who is appointed for a wakf having a gross annual income of not less than five lakhs rupees shall ensure that the budget of the wakf is submitted, the accounts of the wakf are regularly maintained, and the yearly statement of accounts are submitted within such time as the Board may specify.
                                    </p>
                                    <p class="pb20">
                                        <b>(3) </b>While exercising his powers and discharging his functions under sub-section(2), the Executive Officer shall not interfere with any religious duties or any usage or custom of the wakf sanctioned by the Muslim law. 
                                    </p>
                                    <p class="pb20">
                                        <b>(4) </b>The salaries and allowances of the Executive Officer and his staff shall be paid by the Board and in fixing the quantum of such salary the Board shall have due regard to the income of the wakf, the extent and nature of the duties of the duties of the Executive Officer and shall also ensure that amounts of such salaries and allowances are not disproportionate to the income of the wakf and do not operate as an unnecessary financial burden on it.
                                    </p>
                                    <p class="pb20">
                                        <b>(5) </b>The salaries and allowances of the Executive Officer and his staff shall be paid by the Board from the Wakf Fund and, if the wakf generates any additional income as a result of appointment of the Executive Officer, the Board may claim reimbursement of amounts spent on the salaries and allowances from the fund of the wakf concerned.
                                    </p>
                                    <p class="pb20">
                                        <b>(6) </b>The Board may, for sufficient reasons, and after giving to the Executive Officer or a member of his staff, reasonable opportunity of being heard, suspend, remove or dismiss the Executive Officer or a member of his staff from his post.
                                    </p>
                                    <p class="pb20">
                                        <b>(7) </b>Any Executive Officer or a member of his staff who is aggrieved by any order of removal or dismissal made under sub-section(6) may, within thirty days from the date of communication of the order, prefer an appeal against the order to the Tribunal and the Tribunal may, after considering such representation as the Board may make in the matter, and after giving a reasonable opportunity to the Executive Officer or a member of his staff of being heard, confirm, modify or reverse the order.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">Powers of Board in relation to wakfs which have ceased to exist.-</h4>
                                    </div>
                                    <p class="pb20">
                                        <b>(1) </b>The Board shall, if it is satisfied that the objects or any part thereof, of a wakf have ceased to exist, whether such cesser took place before or after the commencement of this Act, cause an inquiry to be held by the Chief Executive Officer, in the prescribed manner, to ascertain the properties and funds pertaining to such wakf.
                                    </p>
                                    <p class="pb20">
                                        <b>(2) </b>On the receipt of the report of inquiry of the Chief Executive Officer, the Board shall pass an order-
                                    </p>
                                    <ol class="pl70 listomy">
                                        <li> specifying the property and funds of such wakf; </li>

                                        <li>directing that any property or funds pertaining to such wakf have been recovered shall be applied or utilized for the renovation of any wakf property and where there is no need for making any such renovation or utilization of the funds is not possible, be appropriated, to any of the purposes specific in sub-clause (iii) of clause (e) of sub-section (2) of Section 32.</li>
                                    </ol>
                                    <p class="pb20">
                                        <b>(3) </b>The Board may, if it has reason to believe that any building or other place which was being used for religious purpose or instruction or charity has, whether before or after the commencement of this act, ceased to be used for that purpose, make an application to the Tribunal for an order directing the recovery of possession of such building or other place.
                                    </p>
                                    <p class="pb20">
                                        <b>(4) </b>The Tribunal may, if it is satisfied, after making such inquiry as it may think fit, that such building or other place-
                                    </p>
                                    <ol class="pl70 listomy">
                                        <li> is wakf property;</li>

                                        <li> has not been acquired under any law for the time being in force relating to acquisition of land or is not under any process of acquisition under any such law, or has not vested in the State Government under any law for the time being in force relating to land reforms; and</li>

                                        <li> is not in the occupation of any person who has been authorised by or under any law for the time being in force to occupy such building or other place, make an order-
                                            <ol class="pl140 listomy">
                                                <li> directing the recovery of such building or place from any person who may be in unauthorised possession thereof, and</li>

                                                <li> directing that such property, building or place be used for religious purpose or instruction as before, or if such use is not possible, be utilised for any purpose specified in sub-clause (iii) of clause (e) of sub-section (2) of Section 32.</li>
                                            </ol>
                                        </li>
                                    </ol>
                                    <div class="title-post">
                                        <h4 class="headg">Decision if a property is wakf property.-</h4>
                                    </div>
                                    <p class="pb20">
                                        <b>(1) </b>The Board may itself collect information regarding any property which it has reason to believe to believe to be wakf property and if any question arises whether a particular property is wakf property or not or whether a wakf is a Sunni wakf or a Shia wakf it may, after making such inquiry as it may deem fit, decide the question.
                                    </p>
                                    <p class="pb20">
                                        <b>(2) </b>The decision of the Board on a question under sub-section (1) shall, unless revoked or modified by the Tribunal, be final.
                                    </p>
                                    <p class="pb20">
                                        <b>(3) </b>Where the Board has any reason to believe that any property of any trust or society registered in pursuance of the Indian Trusts Act, 1882 (2 of 1882) or under the societies Registration Act, 1860 (21 of 1860) or under any other Act, is wakf property, the Board may notwithstanding anything contained in such Act, hold an inquiry in regard to such property and if after such inquiry the Board is satisfied that such property is wakf property, call upon the trust or society, as the case may be, either to register such property under this Act as wakf property or show cause why such property should not be so registered:<br /><br ?>Provided that in all such cases, notice of the action proposed to be taken under this sub-section shall be given to the authority by whom the trust or society has been registered.
                                    </p>
                                    <p class="pb20">
                                        <b>(4) </b>The Board shall, after duly considering such cause as may be shown in pursuance of notice issued under sub-section (3), pass such orders as it may think fit and the order so made by the Board, shall be final, unless it is revoked or modified by a Tribunal.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">Power to cause registration of wakf and to amend register.-</h4>
                                    </div>
                                    <p class="pb20">
                                        The Board may direct a mutawalli to apply for the registration of a wakf, or to supply any information regarding a wakf or may itself cause the wakf to be registered or may at any time amend the register of wakfs.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">Change in the management of wakfs to be notified.-</h4>
                                    </div>
                                    <p class="pb20">
                                        <b>(1) </b>In the case of any change in the management of a registered wakf due to the death or retirement or removal of the mutawalli, the incoming mutawalli shall forthwith, and any other person may notify the change to the Board.
                                    </p>
                                    <p class="pb20">
                                        <b>(2) </b>In the case of any other change in any of the particulars mentioned in section 36, the mutawalli shall, within three months from the occurrence of the change, notify such change to the Board.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">Wakfs registered before the commencement of this Act deemed to be registered.-</h4>
                                    </div>
                                    <p class="pb20">
                                        Notwithstanding anything contained in this chapter, where any wakf has been registered before the commencement of this Act, under any law for the time being in force, it shall not be necessary to register the wakf under the provisions of this Act and any such registration made before such commencement shall be deemed to be a registration made under this Act
                                    </p>                     
