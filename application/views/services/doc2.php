                                        <h4 class="head4">Registering a Partnership Firm in India</h4>
                                    </div>
                                    <p class="pb20">
                                        Partnerships in India are governed by the Indian Partnership Act, 1932 and as per this act a partnership deed may be registered or unregistered.However, there are certain benefits of registering a partnership firm which have been explained here. The successful working of a partnership depends upon the mutual confidence and utmost good faith among the partners because each partner is an agent of others and binds them to the fullest extent of their fortunes. It is, therefore advisable that the partners should be selected with extreme care and caution. And there goes the saying: - “Do not be in too great a hurry while selecting a partner. Choosing a partner is like choosing a wife, marry in haste and repent at leisure”. If the working of the partnership firm goes smooth and there is a good understanding between the partners within themselves and with the outsiders, registration may not be required. However, as future is highly uncertain, it is always advisable to register a partnership firm and the following are the benefits of registering a partnership firm.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="head4">Benefits of Registering a Partnership Firm</h4>
                                    </div>
                                    <p class="pb20">
                                        The benefits of registering a partnership firm are mentioned in Sec 69 of the Indian Partnership Act and they have been explained below:-
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">1. POWER TO FILE CASE IN A COURT BY A PARTNER AGAINST THE FIRM OR OTHER CO-PARTNERS</h4>
                                    </div>
                                    <p class="pb20">
                                        If any dispute arises among the partners or between a partner and the firm or between a partner and ex-partners, and the dispute is based upon the rights arising from contract (i.e. partnership deed) or upon the rights conferred by the Partnership Act, then a partner of a registered firm can always file a case in the court. This power is not available to the partner of an unregistered firm. However, a criminal proceeding can be brought by a partner of an unregistered firm against the other partner(s). Thus, if a partner steals the property of the firm or puts fire to the buildings of the firm, any partner can prosecute him for the same.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">2. POWER TO FILE CASE IN COURT BY FIRM AGAINST 3RD PARTIES</h4>
                                    </div>
                                    <p class="pb20">
                                        The partners of a registered firm can always file a case in the court (if required), to enforce any right arising from contract e.g. for the recovery of the price of goods supplied. This power is not available to the partners of an unregistered firm (except in case of criminal proceeding).It should however be noted that although an unregistered firm cannot file case against 3rd party, the 3rd party always has the power to file a case against both registered as well as unregistered firm.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="headg">3.  POWER TO CLAIM SET-OFF</h4>
                                    </div>
                                    <p class="pb20">
                                        If a 3rd party sues the firm to recover a sum of money the registered firm can always claim a set-off i.e. the registered firm can say that the 3rd party also owes some money to the firm and the same should be adjusted against the claim in question. This power is not available to an unregistered firm. The above are some of the major benefits of registering a partnership firm and all partnership firms are advised to register themselves with the registrar of firms. It should however be noted that registration for Income Tax purposes is different from registration with the Registrar of Firms.Registration with Income Tax Department is mandatory for all Partnership Firms.
                                    </p>                                    
