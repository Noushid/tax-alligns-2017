                                <div class="title-post">
                                    <h4 class="headg">INPUT VAT SETOFF</h4>
                                </div>
                                <p class="pb20">
                                    <b>WHAT IS INPUT VAT</b><br />
                                    Input VAT is the VAT paid by the registered business on the bought of goods and services. In other words, the tax paid by the businesses at the time of purchase or receipt of goods and services known as input VAT.</p>
                                <p class="pb20">
                                    <b>WHAT IS INPUT VAT SET OFF</b><br />
                                    The process of payment of tax liability after adjusting the input tax paid on purchase called input vat set off. There are few points to be noted relating to input vat set off
                                </p>
                                <p class="pb20">
                                    <b>EXPENSES ALLOWED TO CLAIM INPUT VAT</b><br />
                                    VAT on expenses that were incurred by a business can be deducted in the following circumstances:
                                </p>
                                <ol class="pl70 listomy">
                                    <li>The business must be a taxable person (the end consumer cannot claim any input tax refund).</li>
                                    <li>VAT should have been charged correctly (i.e. unduly charged VAT is not recoverable).</li>
                                    <li>The business must hold documentation showing the VAT paid (e.g. valid tax invoice).</li>
                                    <li>The goods or services acquired are used or intended to be used for making taxable supplies.</li>
                                    <li>VAT input tax refund can be claimed only on the amount paid or intended to be paid before the expiration of 6 months after the agreed date for the payment of the supply.</li>
                                </ol>
                                <p class="pb20">
                                    <b>DISALLOWANCES OF INPUT VAT</b><br />
                                    VAT will not be deductible in respect of expenses incurred for making non-taxable supplies. Furthermore, input tax cannot be deducted if it is incurred in respect of specific expenses such as entertainment expenses e.g. employee entertainment.
                                </p>
                                <p class="pb20">
                                    <b>partial exemptioN OF INPUT VAT</b><br />
                                    Where a VAT registered person incurs input tax on its business expenses, this input tax can be recovered in full if it relates to a taxable supply made, or intended to be made, by the registered person. In contrast, where the expense relates to a non-taxable supply (e.g. exempt supplies), the registered person may not recover the input tax paid.<br /><br />In certain situations, an expense may relate to both taxable and non-taxable supplies made by the registered person (such as activities of the banking sector). In these circumstances, the registered person would need to apportion input tax between the taxable and non-taxable (exempt) supplies.
                                </p>
                                <p class="pb20">
                                    <b>offset customs duty against VAT</b><br />
                                    VAT shall be payable in addition to the custom duties paid by the importer of the goods and cannot be deducted. VAT shall be computed on the value that includes the customs duties.
                                </p>
                                <p class="pb20">
                                    <b>WHAT IS bad debt relief UNDER VAT</b><br />
                                    VAT registered businesses will be able to reduce their output tax liability by the amount of VAT that relates to bad debt which has been written off by the VAT registered business. The legislation will include the conditions and limitations concerning the use of this relief.
                                </p>
                                <p class="pb20">
                                    <b>REFUND OF VAT</b><br />
                                    Refunds will be made after the receipt of the application and subject to verification checks, with a particular focus on avoiding fraud.
                                </p>
                                <div class="title-post">
                                    <h4 class="headg">VAT TO REAL ESTATE</h4>
                                </div>
                                <p class="pb20">
                                    <b>REAL ESTATE AND VAT</b><br />The VAT treatment of real estate will depend on whether it is a commercial or residential property.<br /><br />Supplies (including sales or leases) of commercial properties will be taxable at the standard VAT rate (i.e 5%).<br /><br />On the other hand, supplies of residential properties will generally be exempt from VAT. This will ensure that VAT would not constitute an irrecoverable cost to persons who buy their own properties. In order to ensure that real estate developers can recover VAT on construction of residential properties, the first supply of residential properties within 3 years from their completion will be zero-rated.
                                </p>
                                <div class="title-post">
                                    <h4 class="headg">VAT SPECIAL TERMS</h4>
                                </div>
                                <p class="pb20">
                                    <b>VAT GROUPING</b><br />
                                    Businesses that satisfy certain requirements covered under the Legislation (such as being resident in the UAE and being related/associated parties) will be able to register as a VAT group. For some businesses, VAT grouping will be a useful tool that would simplify accounting for VAT.</p>
                                <p class="pb20">
                                    <b>MARGINE SCHEME</b><br />
                                    To avoid double taxation where second hand goods are acquired by a registered person from an unregistered person for the purpose of resale, the VAT-registered person will be able to account for VAT on sales of second hand goods with reference to the difference between the purchase price of the goods and the selling price of the goods (that is, the profit margin). The VAT which must be accounted for by the registered person will be included in the profit margin. The legislation will include the details of the conditions to be met in order to apply this mechanism.
                                </p>
                                <p class="pb20">
                                    <b>VAT ON IMPORT OF GOODS AND SERVICES</b><br />
                                    VAT is due on the goods and services purchased from abroad.In case the recipient in the State is a registered person with the Federal Tax Authority for VAT purposes, VAT would be due on that import using a reverse charge mechanism.<br /><br />In case the recipient in the State is a non-registered person for VAT purposes, VAT would be paid on import of goods from a place outside the GCC. Such VAT will typically be required to be paid before the goods are released to the person.
                                </p>
                                <p class="pb20">
                                    <b>VAT CLAIM FOR UAE nationals</b><br />
                                    A scheme will be introduced to allow a UAE national who is not registered for VAT to reclaim VAT paid on goods and services relating to constructing a new residence which will be privately used by the person and his family. This will allow the recovery of VAT on such expenses as contractor’s services and building materials.
                                </p>
                                <p class="pb20">
                                    <b>REVERSE CHARGE MECHANISM</b><br />
                                    Some time registered business may have to purchase goods from unregistered dealers. As the unregistered dealer has no the responsibility to account VAT, the registered dealer has to account VAT on such purchase and can claim input later. This reversal of tax liability is called Reverse Charge Mechanism
                                </p>
                                <p class="pb20"></p>
                                <div class="title-post">
                                    <h4 class="headg">VAT AND UAE GOVERNMENT</h4>
                                </div>
                                <p class="pb20">
                                    <b>Government Entities under VAT</b><br />
                                    Supplies made by government entities will typically be subject to VAT. This will ensure that government entities are not unfairly advantaged as compared to private businesses.<br /><br />Certain supplies made by government entities will, however, be excluded from the scope of VAT if they are not in competition with the private sector or where the entity is the sole provider of such supplies. It is likely certain government entities will be entitled to VAT refunds – this is designed to avoid budgeting issues and provide a level playing field between outsourced and in sourced activities.<br /><br />For the supplies provided for government entities, the treatment of such supplies shall depend on the same supply and not on the recipient of the supply. Therefore, if the supply is subject to the standard tax rate, the treatment would remain the same even if it is provided to a government entity.
                                </p>
