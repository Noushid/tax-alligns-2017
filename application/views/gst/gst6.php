                                        <h4 class="head3">Treatment of Export Under GST</h4>
                                    </div>
                                    <p class="pb20">
                                        The export of goods or services is considered as a zero-rated supply. GST will not be levied on export of any kind of goods or services.
                                    </p>
                                    <p class="pb20">A duty drawback was provided under the previous laws for the tax paid on inputs for the export of exempted goods. Claiming the duty drawback was a cumbersome process. Under GST, the duty drawback would only be available for the customs duty paid on imported inputs or central excise paid on certain petroleum or tobacco products used as inputs or fuel for captive power generation. There was some confusion surrounding the refund of the tax paid by exporters on the inputs.</p>
                                    <p class="pb20">A guidance note relating to the above issue was released by the Indian government which has helped in clearing doubts regarding the claim of input tax credit on zero-rated exports. An exporter dealing in zero-rated goods under GST can claim a refund for zero-rated supplies as per the following options:</p>
                                </div>
                            </div>
                            <div class="post-case-single v1">
                                <div class="content-post v1">
                                    <div class="title-post">
                                        <h4 class="title">Letter of Undertaking</h4>
                                        <p class="pb20">Only the exporters who have the star category with DGFT can export under this scheme. To get a ‘star’ listing, the exporter should have a foreign inward remittance of 10% of the 2016-107 export turnovers and that should be not less than Rs.1 cr. Letter of undertaking can obtain from DGFT as per the norms issued by them</p>
                                        <h4 class="title">Bond with Bank Guarantee</h4>
                                        <p class="pb20">This is the scheme offered to those exporters, who have not listed in ‘star, category of DGFT. Under this scheme, the exporters have to submit a BOND with CBEC/CGST accompanied with a bank guarantee. The bank guarantee should be an amount not less than 15% of the IGST computed on the value of export consignment.</p>
                                        <p class="pb20">Supply goods or services, or both, under bond or Letter of Undertaking, subject to such conditions, safeguards and procedure as may be prescribed, without payment of integrated tax, and then claim a refund of unutilized input tax credit.</p>
                                        <p class="pb20">The exporter needs to file an application for refund on the common portal either directly or through the facilitation center notified by the GST commissioner. An export manifest or report has to be filed under the Customs Act prior to filing an application for refund.</p>
                                        <h4 class="title">Pay and Refund IGST</h4>
                                        <p class="pb20">Any exporter can paying the IGST, can claim refund of such tax paid on the supplied goods or services, or both. The applicant has to apply for the refund as per the conditions specified under section 54 of the CGST Act.</p>
                                        <p class="pb20">Any exporter or United Nations or Embassy or other agencies/bodies as specified in section 55 who supplies goods or services, or both, after fulfilling certain conditions, safeguards and procedures as may be prescribed; and </p>
                                        <p class="pb20">An exporter is required to file a shipping bill for the goods being exported out of India. In this case, the shipping bill is considered as a deemed application for refund for the IGST paid. It would be deemed to have been filed only when the person in charge of the shipment files the export manifest or report, mentioning the number and date of the shipping bills.</p>
                                        <p class="pb20">Electronic as well as manual shipping bill formats are amended by the department to include GSTIN and IGST. The modified forms are available on the official department website. The Department is also in the process of relaxing the factory stuffing procedure and necessary permissions, to give a boost to the Indian export industry under GST.</p>    
