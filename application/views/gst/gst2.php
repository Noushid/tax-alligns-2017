                            <h3 class="title">Invoicing & bill of supply</h3>
                            <p>
                                GST defines a transaction as ‘Supply’ when there is a transfer, exchange, rental, lease, barter, disposal or license of goods or services. Whenever a transaction takes place, a tax invoice has to be issued depending on the occurrence of any such event or within a prescribed time limit. Hence, every taxpayer registered under the GST network shall be required to issue a tax invoice for the supply of goods or services.
                            </p>
                            <p class="pt20 pb30">
                                Tax invoices have to be raised under certain circumstances. In the case of supply of goods, the invoices shall be raised within the prescribed time as enumerated below.
                            </p>
                        </div>
                        <div class="post-wrap blog-v2 clearfix">
                            <article class="clearfix">
                                <div class="content-post">
                                    <ul class="listomy">
                                        <li>When there is actual movement of goods, then before or at the time of removal of such goods.</li>
                                        <li>If there is no movement involved, then earlier of delivery or making available of such goods.</li>
                                        <li>In case of successive issuance of goods, then earlier of each such issuance.</li>
                                        <li>On the receipt of goods when on GST is applicable on a reverse charge basis</li>
                                        <li>Within 30 days from the actual supply</li>
                                        <li>In case of continuous supply where due date can be ascertained, then 30 days from such due date</li>
                                    </ul>
