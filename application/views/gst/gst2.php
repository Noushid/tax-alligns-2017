                                    <p>
                                        The Government of India has come out with a sample GST Invoice format. A sample format is shown below. It is better to issue invoices on the same lines as the example since your Input Tax Credit largely depends on the Invoice Number and its proper reporting. The serial number of the invoice forms the basis of mismatch or matching the invoices between the supplier and the receiver, giving a seamless, hassle-free credit flow.
                                    </p>
                                </div>
                                <div class="content-post v1 pt20">
                                    <div class="title-post">
                                        <h4 class="title">GST Invoice Format</h4>
                                    </div>
                                    <p class="pb20">There is certain crucial information that needs to be mentioned mandatorily in the GST invoice. These are:</p>
                                    <div class="list-post">
                                        <h4 class="title">Name, address and the GSTIN of the supplier</h4>
                                        <h4 class="title">The nature of invoice (tax invoice, supplementary invoice or revised invoice)</h4>
                                        <h4 class="title">Invoice number (this shall be a consecutive alpha-numeric or numeric series, specific for a financial year)</h4>
                                        <h4 class="title">Date of Invoice</h4>
                                        <h4 class="title">Name, address and the GSTIN of the recipient</h4>
                                        <h4 class="title">Where the value of the goods exceeds Rupees Fifty Thousand and the recipient is an unregistered person, then name and address of such recipient and the delivery address of the consignment.</h4>
                                        <h4 class="title">Description of the goods or services</h4>
                                        <h4 class="title">HSN code of the goods or the Accounting Code of the Services</h4>
                                        <h4 class="title">Quantity of the goods or services</h4>
                                        <h4 class="title">Total value of the goods or services</h4>
                                        <h4 class="title">Rate of Tax on each item</h4>
                                        <h4 class="title">Tax amount charged, on account of CGST, IGST, and SGST to be shown separately under different columns</h4>
                                        <h4 class="title">Name of the supplying State and the place of supply</h4>
                                        <h4 class="title">Place of delivery</h4>
                                        <h4 class="title">A statement mentioning whether reverse charge is applicable or not</h4>
                                        <h4 class="title">Trade Discounts not forming part of value of the goods, if any</h4>
                                        <h4 class="title">Signature in physical form or Digital Signature of the supplier or an authorized person, duly certifying the invoice In addition to the above particulars, an export invoice shall include the following.</h4>
                                        <h4 class="title">A mandatory statement mentioning these specific words – “SUPPLY MEANT FOR EXPORT ON PAYMENT OF IGST” or “SUPPLY MEANT FOR EXPORT UNDER BOND WITHOUT PAYMENT OF IGST.”</h4>
                                        <h4 class="title">Country of destination</h4>
                                        <h4 class="title">Delivery address</h4>
                                        <h4 class="title">The Number and date of application of form for removal, i.e. Form ARE-1</h4>
                                    </div>
                                    <p class="pb20">
                                        Likewise, when an Input Service Distributor issues the invoice, then “Amount of credit distributed” shall also be added to the invoice instead of the rate and value of the goods or services.
                                    </p>
                                    <p class="pb20">
                                        If you are a Goods Transport Agency, you are a critical link in the supply chain and has to include the following in your invoice.
                                    </p>
                                    <div class="list-post">
                                        <h4 class="title">Name and address of the consignor and the consignee</h4>
                                        <h4 class="title">Registered Vehicle number</h4>
                                        <h4 class="title">Gross weight of the consignment</h4>
                                        <h4 class="title">Place of Origin</h4>
                                        <h4 class="title">Destination</h4>
                                        <h4 class="title">GSTIN of the person liable to pay tax</h4>
                                    </div>
                                    <p class="pb20">
                                        The transporter does not require to the Duplicate copy of the Invoice. Instead, they can opt for Invoice Reference Number, which can be generated by the supplier by uploading the tax invoice onto the GST Portal. The portal shall generate a number that is valid for 30 days from such date.
                                    </p>
                                    <p class="pb20">
                                        Apart from the tax invoice, other important documents include Supplementary Invoice, Revised Invoice, Debit or Credit Notes, and Bill of Supply. Let us discuss each one in details.
