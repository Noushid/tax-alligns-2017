                                    </div>
                                    <p class="pb20">
                                        If it is not possible to determine the time of supply under (a), (b) or (c), the time of supply shall be the <b>date of entry</b> in the books of account of the recipient.
                                    </p>
                                    <p class="pb20">
                                        For clause (b)- the date of payment shall be <b>earlier</b> of-
                                    </p>
                                    <div class="list-post">
