                                        <h4 class="title">The date on which the payment is debited from his bank account</h4>
                                    </div>
                                    <p class="pb20"><b>Example:</b></p>
                                    <div class="list-post">
                                        <h4 class="title">Date of receipt of goods 15th May 2018</h4>
