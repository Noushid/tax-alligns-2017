                                        <h4 class="title">Date of entry in books of receiver 18th May 2018</h4>
                                    </div>
                                    <p><b>Time of supply of goods 15th May 2018</b></p>
                                    <p class="pb20">If for some reason time of supply could not be determined supply under (1), (2) or (3) then it would be 18th May 2018 i.e., date of entry</p>
