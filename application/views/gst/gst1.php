                                <h3 class="head3">What is SGST, CGST & IGST ?</h3>
                                <p class="pb20">
                                    Goods & Service Tax or GST will be levied on goods and services. It will replace all the various taxes and bring them under one umbrella to make compliance easier. It will replace the major central and state taxes. GST is a consumption based tax, i.e., the state shall collect tax in which goods were consumed.
                                </p>
                                <div class="content-post v3">
                                    <div class="title-post"><h4 class="title">GST = SGST + CGST + IGST</h4></div>
                                    <div class="pl25">
                                        <p>
                                            GST targets ONE NATION ONE TAX by keeping the interest of both central and state government to resource the fund. India is a federal country where both the Centre and the States have the powers to levy and collect taxes. The mechanism of GSTand the practical side of the SGST, CGST and IGST are given below.
                                        </p>
                                        <p class="pt10">
                                            Levy of the GST is on the basis of supply of goods and services. SGST/CGST/IGST is to be levied by considering the nature of transaction, ie, intra-state or inter-state.
                                        </p>
                                        <p class="pt10">
                                            <b>CGST & SGST shall be imposed on Intra-state transaction</b> = Transaction within the state (Eg:- Supply of goods and services from Calicut to Ernakulam)
                                        </p>
                                        <p class="pt10">
                                            <b>IGST shall be imposed on Inter-state transaction</b> = Transaction from one state to another state (Eg:- Supply of goods and services from Kerala to Tamil Nadu)
                                        </p>
                                    </div>
                                    <div class="title-post pt30"><h4 class="title">1. CGST & SGST</h4></div>
                                    <div class="pl25">
                                        <p>
                                            The central government has the power to levy and collect GST on the supply of goods & services within the same state, known as CGST. At the same time the State governments also have the power to levy and collect GST on the supply of goods & services within the same state, known as SGST. In short, on intra-state transactions, GST rate shall be shared by centre and state equally.
                                        </p>
                                        <p class="pt10"><b>(5%=2.5+2.5<>12%=6+6<>18%=9+9<>28%=14+14)</b></p>
                                        <p class="pt10">
                                            For e.g.: – A dealer in Calicut sold goods to the consumer in Ernakulam worth Rs. 1,00,000. The GST rate of the item is 5%.<br/>This is a transaction within the same state (Kerala) and termed as intra-state supply. In which the 2.5% tax amount will go to Central Government as CGST and other 2.5% will goes to Kerala Government as SGST.
                                        </p>
                                        <p class="pt10">So;</p>
                                        <p class="pt10">
                                            1,00,000 x 2.5% = Rs.2,500 to Central Government 1,00,000 x 2.5% = Rs.2,500 to Kerala Government<br />Total Rs.5,000 (GST @ 5%) levied and collected by Centre and State.
                                        </p>
                                    </div>
                                    <div class="title-post pt30"><h4 class="title">2. IGST</h4></div>
                                    <p>
                                        The central government only has the power to levy and collect GST on the supply of goods & services from one state to another state, known as IGST. State government has no roll in IGST.
                                    </p>
                                    <p class="pt10">
                                        For e.g.: – A dealer in Kerala sold goods to the consumer in Tamil Nadu worth Rs. 1,00,000. The GST rate of the item is 5%. <br />This is a transaction between two states Kerala and Tamil Nadu, termed as inter-state supply. In which the full 5% tax amount will go to Central Government as IGST.
                                    </p>
                                    <p class="pt10">So;</p>    
                                    <p class="pt10">1,00,000 x 5% = Rs.5000 to Central Government. (GST @ 5%) levied and collected by Centre.</p>
                                    <div class="title-post pt30"><h4 class="title">3. INPUT TAX CREDIT</h4></div>
                                    <p class="pt10">
                                        For e.g.: – <br />See the 4 level of transaction of an Item. The GST rate of the item is 5%.
                                        <b>Mohammed Calicut sold goods to Babu Ernakulam worth Rs. 1,00,000.</b><br />
                                        <b>Babu Ernakulam sold goods to Moorthy Tamil Nadu for Rs.1,30,000</b><br />
                                        <b>Moorthy Tamil Nadu sold the goods to Final Consumer in Tamil Nadu for Rs.1,50,000</b><br />
                                        <b>a) Mohammed Calicut sold goods to Babu Ernakulam worth Rs. 1,00,000.</b><br />
                                    </p>
                                    <p class="pt10">
                                        Mr.Babu paid input tax Rs.2500 (2.5%) to Centre as CGST and Rs.2500 (2.5%) to Kerala Govt.<br />In second transaction Mr.Babu has a tax liability of Rs.6500 as IGST (5% of Rs.130000) for the sales to Moorthy Tamil Nadu. But he has to pay tax (IGST) after set off the credit of CGST Rs.2500 + SGST Rs.2500 (Rs.5000). 
                                    </p>
                                    <p class="pt10">
                                        <b>The net tax (IGST) he should pay will be Rs.1500 (6500-5000)</b><br />In third transaction Mr.Moorthy has a levy of Rs.7000 (Rs.3750 (2.5%) to Centre as CGST + Rs.3750 (2.5%) to Tamil Nadu Govt.)<br />But he has to pay tax after set off the credit of IGST Rs.6500. When the input in our credit is IGST we should first set off IGST, then CGST and SGST.
                                    </p>
                                    <p class="pt10">
                                        CGST payable Rs.3750 – Rs.6500 (IGST Credit) = Rs.2750 (balance in IGST credit)<br />SGST payable Rs.3750 – Rs.2750 (IGST Credit balance) = Rs.1000 SGST payable<br />
                                        <b>The net tax (SGST) he should pay will be Rs.1000(CGST+SGST Rs.7500-(IGST Rs.6500))</b>
                                    </p>
