                                    <div class="title-post">
                                        <p class="pb20">This means works contract will be treated as service and tax would be charged accordingly (not as goods or part goods/part services).</p>
                                        <p class="pb20">This treatment of works contract as service and not as supply of goods will bring in much needed clarification to the works contracts. Under the current regime, different states have different schemes for VAT. There are different composition schemes with different VAT rates. Service tax too is complex with 60% abatement on new works and 30% abatement on repair contracts. GST will solve such with a much simpler straightforward calculation</p>
                                    </div>
                                    <div class="title-post">
                                        <h4 class="title">Input Tax Credit is NOT available</h4>
                                        <p>Revised Model GST Law mentions that input tax credit is not available for-</p>
                                        <ul class="listomy">
                                            <li>works contracts <b>services</b> when supplied for construction of immovable property, other than plant and machinery, except where it is an input service for further supply of works contract service</li>
                                            <li><b>goods or services</b> received by a taxable person for construction of an immovable property on his own account, other than plant and machinery, even when used in course or furtherance of business.</li>
                                        </ul>
                                        <p class="pb20">On analysis, one finds this provision is quite contradictory.</p>
                                        <p class="pb20">For example, any contractor/builder will not enjoy any input tax credit on the input services if he constructs any building. But he will enjoy input tax credit on input service for further supply of works contract service. These two sentences are confusing and contradictory.</p>
                                        <p class="pb20">Input tax credit is available to both a builder and a taxable person while constructing plant and machinery. But input tax credit is not available to any taxable person who constructs on his own account even if it is for business use.</p>
                                        <p class="pb20">This entire section is confusing and contradictory and further explanations are required.</p>
                                    </div> 
                                    <div class="title-post">
                                        <h4 class="title">Abatement is not Mentioned</h4>
                                        <p class="pb20">No abatement has been prescribed for works contract service so far. Currently VAT is payable on the works contract. Service tax is paid @15% on either 40% (on new work) or 70% (on repair, maintenance work).</p>
                                        <p class="pb20">In case no abatement/ composition is provided, it may lead to significant increase in tax burden, especially if such works contract is taxed at Standard GST rate (which is 18%) and even if subjected to lower tax rate (12%).</p>
                                    </div>  
                                    <div class="title-post">
                                        <h4 class="title">Composition Scheme is not Available</h4>
                                        <p class="pb20">Composition scheme is not available to works contractors as it is treated as service under GST. Composition scheme is only available to suppliers of goods. This will be a big blow to the small sub-contractors who cannot opt for composition scheme. They will be forced to register for normal taxation scheme increasing their compliances and costs.</p>
                                        <p class="pb20">While GST has clearly defined works contract as service bringing in some clarity, more information is required especially in the areas of input tax credit and composition schemes. Otherwise the same confusion and lengthy costly legal disputes will be seen also under GST regime.</p>
                                    </div>                                         
