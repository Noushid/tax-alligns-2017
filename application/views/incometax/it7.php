                                            <h4 class="head4">TDS ON RENT: LATEST UPDATES </h4>
                                        </div>
                                        <p class="pb20">Here is what the Finance Minister proposed in his speech: “It is proposed to introduce a new provision in the Income-tax Act to provide for tax deduction at source at the rate of five per cent. by an individual or HUF, other than those whose books of account are required to be audited, while making payment of rent of an amount exceeding Rs. 50,000 per month. It is also proposed to provide that such tax shall be deducted and deposited only once in a financial year through a challan cum-statement. Further, the deductor shall not be required to obtain TAN or file any separate TDS return for this purpose.” Can you explain the change? Under the current laws, Individuals and HUF (other than those liable for tax audit) were out of the scope of section 194-I of the Act which required deduction of tax at source when rents were paid in excess of Rs.1,80,000.00. The government today introduced a new section 194-IB which comes to effect from the 1st June, 2017 has brought these individuals and HUF payments under the rule. So while anyone other than Individuals and HUF deducted TDS while making rent payments (any payment, by whatever name called, under any lease, sublease, tenancy or any other agreement or arrangement for the use of any land or building or both), Individuals and HUF were exempted from doing so under the law. Now both the parties (Individuals and HUF) will be responsible to deduct a tax at source of 5% of the rent collected if the monthly payment to another resident exceeds Rs. 50,000/-. The amount so deducted shall be deposited only once a year through a challan cum statement without the payee being obliged to obtain a TAN or file any separate TDS return for this purpose</p>
