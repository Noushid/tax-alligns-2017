                                        <div class="title-post">
                                            <h4 class="head4">PAYMENT WITHOUT TDS</h4>
                                        </div>
                                        <p class="pb20">Yes, if your gross income is well below the basic exemption limit then you can request the person who is responsible for TDS, to not to deduct tax on such income. For doing the same you have to options:</p>
                                        <p class="pb20">Apply to the Assessing officer under whose jurisdiction you fall in Form 13 to get a certificate approving deduction of tax at a lower rate or NIL rate.</p>
                                        <p class="pb20">Submit a declaration in Form 15G/15H in which you declare that your income is below the basic exemption limit during the financial year and tax is required to be deducted at source. This certificate has to be submitted every year and non-submission may lead to deduction of tax. Please note that Form 15G is for individuals and Form 15H is for senior citizens.</p>
                                        <p class="pb20">One major difference between Form 13 and Form 15G/15H is Form 15G/15H can be issued only by individuals assesses, whereas request in Form 13 can be submitted by any person i.e. individual, partnership firm, company, etc. to the ASSESSING OFFICER to get approval for deduction of taxes at lower or NIL rate.</p>
