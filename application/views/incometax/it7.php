                                            <h4 class="head4">TDS ON IMMOVABLE PROPERTY</h4>
                                        </div>
                                        <p class="pb20">There are mainly two sections that prescribe for deduction of taxes on transaction related to an immovable property:</p>
                                        <p class="pb20">Section 194-I: Section 194-I requires for deduction of tax at source on rental income @ 10% for rent on land & building if the total amount of rent paid/credited or to be paid/to be credited exceeds the cap of Rs 1,80,000/- during a financial year. Please note that individuals and HUFs who are not subject to tax audits under section 44AB need not deduct tax at source on such rental expenses.</p>
                                        <p class="pb20">Section 194IA: Section 194IA came into effect from June 2013 which required deduction of tax by the transferee before making payment to transferor @ 1% of the consideration for immovable property. Any sum paid by way of consideration for the transfer of any immovable property (other than agricultural land) is covered under section 194-IA, provided the consideration for the transfer of an immovable property is not less than Rs. 50 lakhs.</p>
                                        <p class="pb20">Section 194LA: Section 194LA provides for deduction of tax at source @ 10% for the payment to be made to the assessee as a compensation on account of compulsory acquisition of immovable property. Please note that no deduction shall be made under this section where the amount of such payment or, as the case may be, the aggregate amount of such payments to a resident during the financial year does not exceed Rs 250000/-.</p>
