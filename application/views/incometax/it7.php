                                        <div class="title-post">
                                            <h4 class="head4">PENALTY UNDER TDS</h4>
                                        </div>
                                        <p class="pb20">There are several instances where interest, fees, and penalty are levied on non-compliance of TDS provisions. The same are discussed here step by step:</p>
                                        <div class="title-post">
                                            <h4 class="headg">Consequences of non-deduction of TDS</h4>
                                        </div>
                                        <p class="pb20">If a person who was responsible for deducting tax at source fails to do so, then the ASSESSING OFFICER has powers to disallow whole of such expenditure for ascertaining taxable profits. For example, ABC Limited paid a commission of Rs 2,00,000/- during the year to a single person and omitted to deduct tax on the same, then the Assessing Officer has powers to disallow deduction whole of such expenses while ascertaining taxable profits.</p>
                                        <div class="title-post">
                                            <h4 class="headg">Late deduction of TDS</h4>
                                        </div>
                                        <p class="pb20">Tax is to be deducted at the time of payment/credit getting due or payment whichever is earlier. In the terms of income tax, even a single day is counted as a month for the purpose of calculating interest. In cases of late deduction of tax, interest @ 1% per month of the TDS amount subject to maximum amount of TDS is levied. For example, ABC company was supposed to deduct tax of Rs 20000/- on 15th July but instead the same was deducted by the company on 1st August. In this case interest of Rs 200/- (@1% for one month) is required to be paid by the assessee.</p>
                                        <div class="title-post">
                                            <h4 class="headg">Late payment of TDS</h4>
                                        </div>
                                        <p class="pb20">Tax is to be deducted and paid to the credit of government on every 7th day of the succeeding month in which the tax has been deducted, otherwise, interest @ 1.5% per month of TDS amount subject to maximum amount of TDS is levied. For example, ABC Ltd was supposed to deposit TDS of Rs 20000/- deducted in the month of April by 7th of May but fails to deposit the same on time and actually deposited the same in the following month. In this case interest of Rs. 300/- (@ 1.5% for one month) is required to paid by the assessee.</p>
                                        <div class="title-post">
                                            <h4 class="headg">Late filing of return of TDS</h4>
                                        </div>
                                        <p class="pb20">TDS returns are required to be filed in the last month of following quarter i.e. 31st July, 31st October, 31st January and in the case of March it is 31st May. Fees under section 234E are levied @ Rs 200/- per day subject to maximum amount of TDS until the return is filed. Example, M/s ABC, a partnership deducted and paid a total TDS of Rs 40000/- in the first quarter of FY and was supposed to file its TDS return by 31st July but filed its return on 31st August. Total fees of Rs 6200 (200/- per day for 31 says) shall be paid before filing of return.</p>
                                        <p class="pb20">Assessing officer may direct a person who fails to file the statement of TDS within due date to pay penalty minimum of Rs. 10,000 which may extend to Rs.1,00,000. The penalty under this section is in addition to the penalty u/s 234E and also cover the cases of incorrect filing of TDS return.</p>
