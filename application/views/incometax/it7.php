                                        <div class="title-post">
                                            <h4 class="head4">TDS CERTIFICATE</h4>
                                        </div>
                                        <p class="pb20">TDS certificates are issued by the deductor (the person who is deducting tax) to the deductee (the person from whose payment the tax is deducted). There are mainly two types of TDS certificates issued by the deductor.</p>
                                        <p class="pb20">Form 16: which is issued by the employer to the employee incorporating details of tax deducted by the employer throughout the year, and</p>
                                        <p class="pb20">Form 16A: which is issued in all cases other than salary.</p>
                                        <p class="pb20">For example, Mr. Gupta is working as a salaried employee at a company and tax is deducted on his salary @ 15%. The company shall provide Mr. Gupta with a Form 16 describing particulars in detail regarding the amount of salary paid and tax deducted on the same.</p>
                                        <p class="pb20">However, had Mr. Gupta been working as a professional and received professional fees from an organization which is subject to TDS, then he will be provided Form 16A for the same.</p>
                                    </div>
                                    <div id="menu2" class="tab-pane fade">
                                        <div class="title-post">
                                            <h4 class="head4">TDS DEDUCTION</h4>
                                        </div>
                                        <p class="pb20">The concept of TDS is based on a simple principle i.e. tax is to be deducted at the time of payment getting due or actual payment whichever is earlier. A set of scenarios for will be helpful in understanding the concept:</p>
                                        <p class="pb20">Say, ABC Private Limited has to make payment of Rs 50,000/- to Mr. XYZ in exchange of professional services.</p>
                                        <div class="title-post">
                                            <h4 class="headg">EXAMPLE 1:</h4>
                                        </div>
                                        <p class="pb20">Mr. XYZ was paid Rs 30,000/- in advance on 15th July. XYZ raised invoice after completion of work on 31st July and rest of payment is to be made.</p>
                                        <p class="pb20">In such case the company should have deducted tax in the following manner:<br/><br/>On 15th July: Rs 3000/- (@ 10% on advance of Rs 30000/-)<br/>On 31st July: Rs 2000/- (@ 10% of total invoice amount as deducted by tax already deducted i.e. Rs 5000/- deducted by Rs 3000/-)</p>
                                        <div class="title-post">
                                            <h4 class="headg">EXAMPLE 2:</h4>
                                        </div>
                                        <p class="pb20">Mr. XYZ raised the invoice on 15th July and was paid whole consideration at one go on 31st July.</p>
                                        <p class="pb20">In such whole amount of Rs 5000/- shall be deducted on 15th July, the date when payment got due, and a net payment of Rs 45000/- shall be made on 31st July.</p>
                                        <div class="title-post">
                                            <h4 class="headg">EXAMPLE 3:</h4>
                                        </div>
                                        <p class="pb20">Mr. XYZ is to receive the whole amount of Rs 50,000/- well in advance before completion of the assignment.</p>
                                        <p class="pb20">In such particular case tax of Rs 5000/- shall be deducted right at the time of payment of advance and no tax is to be deducted at the time of making an entry for the bill due.</p>
                                    </div>
                                    <div id="menu3" class="tab-pane fade">
                                        <div class="title-post">
                                            <h4 class="head4">TDS ON SALARY</h4>
                                        </div>
                                        <p class="pb20">Persons responsible for paying salary are liable to deduct tax on estimated salary at prescribed rate of 15% subject to following:</p>
                                        <p class="pb20">Exemption Limit: No tax is required to be deducted at source unless the estimated salary exceeds basic exemption limit.</p>
                                        <p class="pb20">Exempt allowances: Allowances such as LTC, HRA, conveyance, travelling exempt as per prescribed limits and other perquisites not forming part of salary should be deducted from total salary while calculating taxable salary.</p>
                                        <p class="pb20">Other deductions: Other deductions such as deductions under section 80C, 80CCC, 80CCD, 80CCG, 80D, 80DD, 80DDB, 80E, 80EE, etc. should be considered before the calculation of tax on salary.</p>
                                        <p class="pb20">If after comprehensive calculation of allowable allowances, taxable perquisites and deductions under chapter VI-A, income from salary head exceeds a sum of basic exemption limit, then tax has to be deducted by the employer @ 15% on the amount over and above the basic exemption limit. For example, the salary of Mr. A arrives at Rs 2,80,000/- assuming that all the allowances, perquisites, and deductions have been taken into consideration, tax @ 15% on Rs 30000/- (2,80,000 – 2,50,000) shall be deducted by the employer.</p>
                                        <p class="pb20">Hence, provisions of TDS shall attract only if minimum salary is above the basic exemption limit.</p>
                                    </div>
                                    <div id="menu4" class="tab-pane fade">
                                        <div class="title-post">
                                            <h4 class="head4">TDS REFUND</h4>
                                        </div>
                                        <p class="pb20">There is this major misconception that refund of excess TDS is different from income tax refund and is called as TDS refund. However, the fact is that there is only one kind of return which you claim while filing your annual income tax return. Nowadays, it is compulsory to quote bank account details such as account number and IFSC code while filing of return and non-entering of such details will not generate a valid .xml file. In case if someone has deducted more tax than he should have deducted, then income tax refund will arise which can be claimed upon the filing of your annual income tax return.</p>
                                        <p class="pb20">For example, you own a goods transport agency and yours is a proprietorship firm. You presented an invoice of Rs 50,000/- and the person paying freight paid you a net amount of Rs 49,000/- (after deducting tax of Rs 1,000/- @ 2% under section 194C). In this case, the deductor deducted tax @ 2% instead of 1% and hence deducted excess TDS by Rs 500/-. This excess TDS will arise as a refund in the income tax return.</p>
