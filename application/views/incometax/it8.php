                                        <h4 class="title">Example for TCS</h4>
                                    </div>
                                    <p class="pb20">
                                        The concept of TCS can be explained with the help of an example. 
                                    </p>
                                    <p class="pb20">
                                        For example: Mr X buys from Mr. Y scrap for Rs. 1000. So Mr. X would be liable to pay Rs. 1010 to Mr Y (1000 for Scrap and 1% of Rs. 1000 i.e. Rs. 10 as TCS). This TCS so collected by Mr. Y would be required to be deposited with the Govt. by Mr Y.Credit would be available to the buyer for the TCS so collected by the seller from the buyer. This TCS can be claimed as a credit by the buyer at the time of payment of taxes computed as per the income tax slab rates of the buyer. The Credit so claimed by the buyer is also required to be disclosed in the income tax return.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="head4">EXCEPTION TO APPLICABILITY OF TCS RATES</h4>
                                    </div>
                                    <p class="pb20">
                                        The requirement of tax collection at source shall not apply in case the buyer (provided he is resident in India), furnishes a declaration in Form 27C (in duplicate) to the person responsible for collecting such TCS, that the goods mentioned above are to be used for the purpose of manufacturing, processing or producing articles or things and not for trading purposes. A copy of the declaration so furnished by the buyer to the seller shall be delivered by the seller to the Chief Commissioner or Commissioner before the 7th day of the month next following the month in which the declaration is furnished by him.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="head4">TAX COLLECTION AT SOURCE AT LOWER RATE</h4>
                                    </div>
                                    <p class="pb20">
                                        If the assessing officer is satisfied that the total income of the buyer justifies the collection of TCS at a lower rate, the assessing officer shall, on an application made by the buyer in Form 13, give to him a certificate, as may be appropriate for the collection of tax ay such lower rate.
                                    </p>
                                    <p class="pb20">
                                        The certificate so issued by the assessing officer shall be valid till the time such certificate is not cancelled by the assessing officer.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="head4">DUTY OF THE PERSON COLLECTING TCS</h4>
                                    </div>
                                    <p class="pb20">
                                        The buyer who has collected the tax at source is required to apply for a TAN No. This TAN No. is required to be quoted in all TCS Return Forms and also at the time of deposit the TCS collected. The due date for depositing TCS with the govt is the same as the due date of depositing TDS. The person deducting the TCS is also required to furnish a TCS Return in the specified form and format stating the nature of goods sold, value of goods sold and the TCS Rate thereon.
