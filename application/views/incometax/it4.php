                                                    <td><b>Less : Expenses incurred in relation to earning of the income</b></td>
                                                    <td>(XXXXX)</td>
                                                </tr>
                                                <tr>
                                                    <td>Taxable Business Income</td>
                                                    <td>XXXXX</td>
                                                </tr>
                                            </tbody>
                                        </table>
                                    </div>
                                    <p class="pb20">
                                        For the purpose of computing taxable business income in the above manner, the taxpayers have to maintain books of account of the business and income will be computed on the basis of the information revealed in the books of account​.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">6. ​What is the manner of computation of taxable business income in case of a person adopting the presumptive taxation scheme of section 44AD ?</h4>
                                    </div>
                                    <p class="pb20">
                                        In case of a person adopting the provisions of section 44AD, income will be computed on presumptive basis, i.e., @ 8% of the turnover or gross receipts of the eligible business for the year.
                                    </p>
                                    <p class="pb20">
                                        Income shall be calculated at rate of 6% in respect of total turnover or gross receipts which is received by an account payee cheque or draft or use of electronic clearing system.
                                    </p>
                                    <p class="pb20">
                                        In other words, in case of a person adopting the provisions of section 44AD​, income will not be computed in normal manner as discussed in previous FAQ (i.e., Turnover less Expense) but will be computed @ 8%/6% of the turnover.
                                    </p>
                                    <p class="pb20">
                                        Income at higher rate, i.e., higher than 8% can be declared if the actual income is higher than 8%. 
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">7. ​As per the presumptive taxation scheme of section 44AD, income of a taxpayer will be computed @ 8%/6% of the turnover or gross receipt and from such income can the taxpayer claim any further deductions ?</h4>
                                    </div>
                                    <p class="pb20">
                                        Under the normal provisions of the Income-tax Law, taxable business income will be computed after allowing deduction in respect of expenses which are deductible as per the Income-tax Actand after disallowing expenses which are not deductible as per the Income-tax Act.In case of a person who is opting for the presumptive taxation scheme of section 44AD, the provisions of allowance/disallowances as provided under the Income-tax Law will not apply and income computed at the presumptive rate of 8%/6% will be the final taxable income of the business covered under the presumptive taxation scheme and no further expenses will be allowed or disallowed.
                                    </p>
                                    <p class="pb20">
                                        While computing income as per the provisions of section 44AD, separate deduction on account of depreciation is not available, however, the written down value of any asset used in such business shall be calculated as if depreciation as persection 32​ is claimed and has been actually allowed. 
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">8. ​If a person adopts the presumptive taxation scheme of section 44AD, then is he required to maintain books of account as per section 44AA ?</h4>
                                    </div>
                                    <p class="pb20">
                                        Section 44AA deals with provisions relating to maintenance of books of account by a person engaged in business/profession. Thus, a person engaged in business/profession has to maintain books of account of his business/profession according to the provisions of section 44AA.
                                    </p>
                                    <p class="pb20">
                                        In case of a person engaged in a business and opting for the presumptive taxation scheme of section 44AD, the provisions ofsection 44AA  relating to maintenance of books of account will not apply. In other words, if a person adopts the provisions ofsection 44AD and declares income @ 8%/6% of the turnover, then he is not required to maintain the books of account as provided under section 44AA in respect of business covered under the presumptive taxation scheme of section 44AD​.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">If a person adopts the presumptive taxation scheme of section 44AD, then is he liable to pay advance tax in respect of income from business covered under section 44AD ?</h4>
                                    </div>
                                    <p class="pb20">
                                        Any person opting for the presumptive taxation scheme under section 44AD is liable to pay whole amount of advance tax on or before 15th March of the previous year. If he fails to pay the advance tax by 15th march of previous year, he shall be liable to pay interest as per section 234C. Note: Any amount paid by way of advance tax on or before 31st day of March shall also be treated as advance tax paid during the financial year ending on that day.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">10. If a person adopts the presumptive taxation scheme but he opts out from the scheme in any of the subsequent five years, then what are the consequences ?</h4>
                                    </div>
                                    <p class="pb20">
                                        ​​If a person opts for presumptive taxation scheme then he is also require to follow the same scheme for next 5 years. If he failed to do so, then presumptive taxation scheme will not be available for him for next 5 years. [For example, an assessee claims to be taxed on presumptive basis under Section 44AD for AY 2017-18. For AY 2018-19 and 2019-20 also he offers income on basis of presumptive taxation scheme. However, for AY 2020-21, he did not opt for presumptive taxation Scheme. In this case, he will not be eligible to claim benefit of presumptive taxation scheme for five AYs, i.e. from AY 2021-22 to 2025-26.]
                                    </p>
                                    <p class="pb20">
                                        He is required to keep and maintain books of account and he is also liable for tax audit as per section 44AB from the AY in which he opts out from the presumptive taxation scheme. [If his total income exceeds maximum amount not chargeable to tax]
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">Can a person who owns more than 10 goods vehicles adopt the presumptive taxation scheme of section 44AE ?</h4>
                                    </div>
                                    <p class="pb20">
                                        The presumptive taxation scheme of ​ sections 44AE can be adopted by a person who is engaged in the business of plying, hiring or leasinggoods carriages and who does not own more than 10 goods vehicles at any time during the year.
                                    </p>
                                    <p class="pb20">
                                        The important criterion of the scheme is the restriction of owning of not more than 10 goods vehicles at any time during the year. Thus, if a person owns more than 10 goods vehicles at any time during the year, then he cannot take advantage of this scheme.
                                    </p>
                                    <div class="title-post">
                                        <h4 class="title">12. ​What is the manner of computation of taxable business income in case of a person adopting the presumptive taxation scheme of section 44AE ?</h4>
                                    </div>
                                    <p class="pb20">
                                        In case of a person who is willing to opt for the presumptive taxation scheme of sections 44AE, income will be computed on an estimated basis. Income will be computed @ Rs. 7,500 per month or part thereof during which the goods vehicle is owned by the taxpayer, in the previous year. Part of the month would be considered as full month.<br />Note: If the actual income is higher, than the presumptive rate, i.e., higher than Rs. 7,500, then such higher income can be declared.
